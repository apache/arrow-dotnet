        /// <summary>
        /// Like TimestampHasTimezone but works on TypeInfo directly (for element types in collections).
        /// </summary>
        private static bool TypeInfoHasTimezone(TypeInfo type)
        {
            var over = type.ArrowTypeOverride;
            if (over == null)
                return true; // default is UTC
            var lower = over.Trim().ToLowerInvariant();
            if (!lower.StartsWith("timestamp"))
                return true;
            var start = lower.IndexOfAny(new[] { '[', '(' });
            if (start < 0)
                return false;
            var end = lower.IndexOfAny(new[] { ']', ')' }, start);
            if (end < 0)
                return false;
            var inner = lower.Substring(start + 1, end - start - 1);
            return inner.Contains(",");
        }

