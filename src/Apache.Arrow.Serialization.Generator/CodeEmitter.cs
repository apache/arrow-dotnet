using System;
using System.Collections.Generic;
using System.Text;

#nullable enable

namespace Apache.Arrow.Serialization.Generator
{

    internal class CodeEmitter
    {
        private readonly StringBuilder _sb;
        private readonly TypeModel _model;
        private int _indent;

        public CodeEmitter(StringBuilder sb, TypeModel model)
        {
            _sb = sb;
            _model = model;
        }

        public void Emit()
        {
            Line("// <auto-generated/>");
            Line("#nullable enable");
            Line("#pragma warning disable CS8629 // Nullable value type may be null");
            Line();
            Line("using System.Collections.Generic;");
            Line("using System.Linq;");
            Line("using Apache.Arrow;");
            Line("using Apache.Arrow.Arrays;");
            Line("using Apache.Arrow.Types;");
            Line("using Apache.Arrow.Serialization;");
            Line();

            if (_model.Namespace != null)
            {
                Line($"namespace {_model.Namespace};");
                Line();
            }

            var typeKeyword = GetTypeKeyword();
            Line($"partial {typeKeyword} {_model.TypeName} : IArrowSerializable");
            Line("{");
            _indent++;

            EmitSchemaField();
            Line();
            EmitSerialize();
            Line();
            EmitDeserialize();
            Line();
            EmitMultiRowSerialize();
            Line();
            EmitMultiRowDeserialize();

            _indent--;
            Line("}");
            Line();
            CodeEmitterHelpers.EmitSerializerRegistration(Line, _model.TypeName);
        }

        private void EmitSchemaField()
        {
            Line($"private static readonly Schema _arrowSchema = new Schema.Builder()");
            _indent++;
            foreach (var prop in _model.Properties)
            {
                var arrowType = prop.ConverterTypeName != null
                    ? $"new {prop.ConverterTypeName}().ArrowType"
                    : GetArrowTypeExpression(prop.Type);
                var nullable = prop.IsNullable ? "true" : "false";
                if (prop.Metadata.Count > 0)
                {
                    var metadataEntries = string.Join(", ",
                        prop.Metadata.ConvertAll(kv =>
                            $"new System.Collections.Generic.KeyValuePair<string, string>(\"{Escape(kv.Key)}\", \"{Escape(kv.Value)}\")"));
                    Line($".Field(new Field(\"{prop.FieldName}\", {arrowType}, {nullable}, new[] {{ {metadataEntries} }}))");
                }
                else
                {
                    Line($".Field(new Field(\"{prop.FieldName}\", {arrowType}, {nullable}))");
                }
            }
            foreach (var kv in _model.Metadata)
            {
                Line($".Metadata(\"{Escape(kv.Key)}\", \"{Escape(kv.Value)}\")");
            }
            Line(".Build();");
            _indent--;
            Line();
            var newKw = _model.HasArrowSerializableBase ? "new " : "";
            Line($"public static {newKw}Schema ArrowSchema => _arrowSchema;");
        }

        private static string Escape(string s) => s.Replace("\\", "\\\\").Replace("\"", "\\\"");

        private void EmitSerialize()
        {
            Line($"public static RecordBatch ToRecordBatch({_model.TypeName} value)");
            Line("{");
            _indent++;

            if (_model.HasSerializationCallback)
            {
                Line("((Apache.Arrow.Serialization.IArrowSerializationCallback)value).OnBeforeSerialize();");
                Line();
            }

            // Build each column array
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitArrayBuilder(prop, i);
            }

            // Build RecordBatch
            Line();
            Line("return new RecordBatch(_arrowSchema, new IArrowArray[]");
            Line("{");
            _indent++;
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var comma = i < _model.Properties.Count - 1 ? "," : "";
                Line($"arr_{i}{comma}");
            }
            _indent--;
            Line("}, 1);");

            _indent--;
            Line("}");
        }

        private void EmitArrayBuilder(PropertyModel prop, int index)
        {
            var varName = $"arr_{index}";
            var access = $"value.{prop.PropertyName}";

            if (prop.IsNullable)
            {
                EmitNullableArrayBuilder(prop, index, varName, access);
                return;
            }

            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.String:
                    {
                        var si = GetStringArrayInfo(prop);
                        Line($"var {varName} = new {si.BuilderType}().Append({access}).Build();");
                        break;
                    }
                case ArrowTypeKind.Bool:
                    {
                        var bl = GetBoolArrayInfo(prop);
                        Line($"var {varName} = new {bl.BuilderType}().Append({access}).Build();");
                        break;
                    }
                case ArrowTypeKind.Byte:
                    Line($"var {varName} = new UInt8Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.SByte:
                    Line($"var {varName} = new Int8Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Int16:
                    Line($"var {varName} = new Int16Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.UInt16:
                    Line($"var {varName} = new UInt16Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Int32:
                    Line($"var {varName} = new Int32Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.UInt32:
                    Line($"var {varName} = new UInt32Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Int64:
                    Line($"var {varName} = new Int64Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.UInt64:
                    Line($"var {varName} = new UInt64Array.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Float:
                    Line($"var {varName} = new FloatArray.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Double:
                    Line($"var {varName} = new DoubleArray.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Binary:
                    {
                        var bi = GetBinaryArrayInfo(prop);
                        Line($"var {varName} = new {bi.BuilderType}().Append({BinaryToSpan(prop, access)}).Build();");
                        break;
                    }
                case ArrowTypeKind.DateTime:
                    Line($"var {varName} = new TimestampArray.Builder({GetTimestampTypeExpr(prop)}).Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}({access})).Build();");
                    break;
                case ArrowTypeKind.DateTimeOffset:
                    Line($"var {varName} = new TimestampArray.Builder({GetTimestampTypeExpr(prop)}).Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}({access})).Build();");
                    break;
                case ArrowTypeKind.DateOnly:
                    {
                        var di = GetDateArrayInfo(prop);
                        Line($"var {varName} = new {di.BuilderType}().Append({access}).Build();");
                        break;
                    }
                case ArrowTypeKind.TimeOnly:
                    {
                        var ti = GetTimeArrayInfo(prop);
                        Line($"var {varName} = new {ti.BuilderType}({ti.TypeExpr}).Append({access}).Build();");
                        break;
                    }
                case ArrowTypeKind.TimeSpan:
                    Line($"var {varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildDurationArray({access});");
                    break;
                case ArrowTypeKind.Decimal:
                    Line($"var {varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildDecimalArray({access});");
                    break;
                case ArrowTypeKind.Guid:
                    Line($"var {varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray({access});");
                    break;
                case ArrowTypeKind.Half:
                    Line($"var {varName} = new HalfFloatArray.Builder().Append({access}).Build();");
                    break;
                case ArrowTypeKind.Enum:
                    EmitEnumArrayBuilder(prop, index, varName, access);
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                    EmitListArrayBuilder(prop, index, varName, access);
                    break;
                case ArrowTypeKind.Set:
                    EmitSetArrayBuilder(prop, index, varName, access);
                    break;
                case ArrowTypeKind.Dictionary:
                    EmitMapArrayBuilder(prop, index, varName, access);
                    break;
                case ArrowTypeKind.NestedRecord:
                    EmitNestedRecordSerializer(prop, index, varName, access);
                    break;
                case ArrowTypeKind.Custom:
                    Line($"var {varName} = new {prop.ConverterTypeName}().ToArray({access});");
                    break;
                default:
                    Line($"// TODO: Unsupported type {prop.Type.Kind} for {prop.PropertyName}");
                    Line($"IArrowArray {varName} = new NullArray(1);");
                    break;
            }
        }

        private void EmitNullableArrayBuilder(PropertyModel prop, int index, string varName, string access)
        {
            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.String:
                    {
                        var si = GetStringArrayInfo(prop);
                        Line($"var {varName} = new {si.BuilderType}().Append({access}).Build();");
                        break;
                    }
                case ArrowTypeKind.Bool:
                    {
                        var bl = GetBoolArrayInfo(prop);
                        Line($"var b_{index} = new {bl.BuilderType}();");
                        Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                        Line($"var {varName} = b_{index}.Build();");
                        break;
                    }
                case ArrowTypeKind.Int32:
                    Line($"var b_{index} = new Int32Array.Builder();");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.Int64:
                    Line($"var b_{index} = new Int64Array.Builder();");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.Float:
                    Line($"var b_{index} = new FloatArray.Builder();");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.Double:
                    Line($"var b_{index} = new DoubleArray.Builder();");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.DateTime:
                    Line($"var b_{index} = new TimestampArray.Builder({GetTimestampTypeExpr(prop)});");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}(v_{index})); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.DateTimeOffset:
                    Line($"var b_{index} = new TimestampArray.Builder({GetTimestampTypeExpr(prop)});");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}(v_{index})); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.DateOnly:
                    {
                        var di = GetDateArrayInfo(prop);
                        Line($"var b_{index} = new {di.BuilderType}();");
                        Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                        Line($"var {varName} = b_{index}.Build();");
                        break;
                    }
                case ArrowTypeKind.TimeOnly:
                    {
                        var ti = GetTimeArrayInfo(prop);
                        Line($"var b_{index} = new {ti.BuilderType}({ti.TypeExpr});");
                        Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                        Line($"var {varName} = b_{index}.Build();");
                        break;
                    }
                case ArrowTypeKind.TimeSpan:
                    Line($"var {varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildDurationArray({access});");
                    break;
                case ArrowTypeKind.Decimal:
                    Line($"var {varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildDecimalArray({access});");
                    break;
                case ArrowTypeKind.Guid:
                    Line($"var {varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray({access});");
                    break;
                case ArrowTypeKind.Half:
                    Line($"var b_{index} = new HalfFloatArray.Builder();");
                    Line($"if ({access} is {{ }} v_{index}) b_{index}.Append(v_{index}); else b_{index}.AppendNull();");
                    Line($"var {varName} = b_{index}.Build();");
                    break;
                case ArrowTypeKind.Enum:
                    EmitEnumArrayBuilder(prop, index, varName, access);
                    break;
                case ArrowTypeKind.NestedRecord:
                    EmitNestedRecordSerializer(prop, index, varName, access);
                    break;
                case ArrowTypeKind.Custom:
                    Line($"IArrowArray {varName};");
                    Line($"if ({access} is {{ }} cv_{index})");
                    Line($"    {varName} = new {prop.ConverterTypeName}().ToArray(cv_{index});");
                    Line($"else");
                    Line($"    {varName} = new NullArray(1);");
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        Line($"IArrowArray {varName};");
                        Line($"if ({access} != null)");
                        Line("{");
                        _indent++;
                        var tempVar = $"_nonnull_{index}";
                        if (prop.Type.Kind == ArrowTypeKind.Set)
                            EmitSetArrayBuilder(prop, index, tempVar, access);
                        else
                            EmitListArrayBuilder(prop, index, tempVar, access);
                        Line($"{varName} = {tempVar};");
                        _indent--;
                        Line("}");
                        Line("else");
                        Line("{");
                        _indent++;
                        Line($"{varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildNullArray(ArrowSchema.GetFieldByName(\"{prop.FieldName}\").DataType, 1);");
                        _indent--;
                        Line("}");
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        Line($"IArrowArray {varName};");
                        Line($"if ({access} != null)");
                        Line("{");
                        _indent++;
                        var tempVar = $"_nonnull_{index}";
                        EmitMapArrayBuilder(prop, index, tempVar, access);
                        Line($"{varName} = {tempVar};");
                        _indent--;
                        Line("}");
                        Line("else");
                        Line("{");
                        _indent++;
                        Line($"{varName} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildNullArray(ArrowSchema.GetFieldByName(\"{prop.FieldName}\").DataType, 1);");
                        _indent--;
                        Line("}");
                        break;
                    }
                default:
                    // Fallback: use the non-nullable path (many builders accept null for ref types)
                    EmitArrayBuilder(new PropertyModel
                    {
                        PropertyName = prop.PropertyName,
                        FieldName = prop.FieldName,
                        Type = prop.Type,
                        IsNullable = false,
                    }, index);
                    break;
            }
        }

        private void EmitEnumArrayBuilder(PropertyModel prop, int index, string varName, string access)
        {
            // Dictionary-encoded string array: indices + dictionary
            // For a single value, build a StringArray for the dictionary and Int16Array for indices
            if (prop.IsNullable)
            {
                Line($"StringArray dict_{index};");
                Line($"Int16Array idx_{index};");
                Line($"if ({access} is {{ }} ev_{index})");
                Line("{");
                _indent++;
                Line($"dict_{index} = new StringArray.Builder().Append(ev_{index}.ToString()).Build();");
                Line($"idx_{index} = new Int16Array.Builder().Append(0).Build();");
                _indent--;
                Line("}");
                Line("else");
                Line("{");
                _indent++;
                Line($"dict_{index} = new StringArray.Builder().Build();");
                Line($"idx_{index} = new Int16Array.Builder().AppendNull().Build();");
                _indent--;
                Line("}");
            }
            else
            {
                Line($"var dict_{index} = new StringArray.Builder().Append({access}.ToString()).Build();");
                Line($"var idx_{index} = new Int16Array.Builder().Append(0).Build();");
            }
            Line($"var {varName} = new DictionaryArray(new DictionaryType(Int16Type.Default, StringType.Default, false), idx_{index}, dict_{index});");
        }

        private void EmitListArrayBuilder(PropertyModel prop, int index, string varName, string access)
        {
            var elemType = prop.Type.ElementType!;
            if (NeedsManualListBuilder(elemType))
            {
                EmitManualListArray(elemType, index, varName, access, prop.FieldName);
                return;
            }
            var arrowElemType = GetArrowTypeExpression(elemType);

            Line($"var lb_{index} = new ListArray.Builder({arrowElemType});");
            Line($"lb_{index}.Append();");
            EmitListValuePopulation(elemType, $"lb_{index}", access, index);
            Line($"var {varName} = lb_{index}.Build();");
        }

        private void EmitSetArrayBuilder(PropertyModel prop, int index, string varName, string access)
        {
            var elemType = prop.Type.ElementType!;
            if (NeedsManualListBuilder(elemType))
            {
                EmitManualListArray(elemType, index, varName, access, prop.FieldName);
                return;
            }
            var arrowElemType = GetArrowTypeExpression(elemType);

            Line($"var lb_{index} = new ListArray.Builder({arrowElemType});");
            Line($"lb_{index}.Append();");
            EmitListValuePopulation(elemType, $"lb_{index}", access, index);
            Line($"var {varName} = lb_{index}.Build();");
        }

        /// <summary>
        /// Returns true for element types that cannot use ListArray.Builder because
        /// ArrowArrayBuilderFactory.Build() does not support them (extension types like Guid).
        /// </summary>
        private static bool NeedsManualListBuilder(TypeInfo elemType)
        {
            return elemType.Kind == ArrowTypeKind.Guid
                || elemType.Kind == ArrowTypeKind.Enum
                || elemType.Kind == ArrowTypeKind.NestedRecord;
        }

        /// <summary>
        /// Returns true for element types that need manual deserialization (can't use GetElementReader).
        /// </summary>
        private static bool NeedsManualListDeserialize(TypeInfo elemType)
        {
            return NeedsManualListBuilder(elemType)
                || elemType.Kind == ArrowTypeKind.List
                || elemType.Kind == ArrowTypeKind.Array
                || elemType.Kind == ArrowTypeKind.Dictionary;
        }

        /// <summary>
        /// Builds a single-row ListArray manually for element types unsupported by ArrowArrayBuilderFactory.
        /// </summary>
        private void EmitManualListArray(TypeInfo elemType, int index, string varName, string access, string fieldName)
        {
            var arrowElemType = GetArrowTypeExpression(elemType);
            switch (elemType.Kind)
            {
                case ArrowTypeKind.Guid:
                    Line($"var guidList_{index} = new System.Collections.Generic.List<System.Guid>({access});");
                    Line($"var guidValues_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray(guidList_{index});");
                    Line($"var offsets_{index} = new ArrowBuffer.Builder<int>();");
                    Line($"offsets_{index}.Append(0);");
                    Line($"offsets_{index}.Append(guidList_{index}.Count);");
                    Line($"var {varName} = new ListArray(new ListType(new Field(\"item\", {arrowElemType}, false)), 1, offsets_{index}.Build(), guidValues_{index}, ArrowBuffer.Empty);");
                    break;
                case ArrowTypeKind.Enum:
                    {
                        // Enum list elements: dictionary-encoded (matching Python schema)
                        Line($"var enumDict_{index} = new StringArray.Builder();");
                        Line($"var enumIdx_{index} = new Int16Array.Builder();");
                        Line($"var enumMap_{index} = new System.Collections.Generic.Dictionary<string, short>();");
                        Line($"foreach (var e_{index} in {access}) {{");
                        Line($"    var es_{index} = e_{index}.ToString();");
                        Line($"    if (!enumMap_{index}.TryGetValue(es_{index}, out var ei_{index})) {{ ei_{index} = (short)enumMap_{index}.Count; enumMap_{index}[es_{index}] = ei_{index}; }}");
                        Line($"    enumIdx_{index}.Append(ei_{index});");
                        Line($"}}");
                        Line($"foreach (var kv in enumMap_{index}.OrderBy(kv => kv.Value)) enumDict_{index}.Append(kv.Key);");
                        Line($"var enumDictArr_{index} = new DictionaryArray(new DictionaryType(Int16Type.Default, StringType.Default, false), enumIdx_{index}.Build(), enumDict_{index}.Build());");
                        Line($"var offsets_{index} = new ArrowBuffer.Builder<int>();");
                        Line($"offsets_{index}.Append(0);");
                        Line($"offsets_{index}.Append(enumDictArr_{index}.Length);");
                        Line($"var {varName} = new ListArray(ArrowSchema.GetFieldByName(\"{fieldName}\").DataType, 1, offsets_{index}.Build(), enumDictArr_{index}, ArrowBuffer.Empty);");
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        // Nested record list elements: serialize via child's ToRecordBatch, wrap in StructArray
                        var typeName = elemType.FullTypeName;
                        Line($"var nestedList_{index} = new System.Collections.Generic.List<{typeName}>({access});");
                        Line($"var nestedBatch_{index} = {typeName}.ToRecordBatch((IReadOnlyList<{typeName}>)nestedList_{index});");
                        Line($"var nestedCols_{index} = new IArrowArray[nestedBatch_{index}.ColumnCount];");
                        Line($"for (int f = 0; f < nestedBatch_{index}.ColumnCount; f++) nestedCols_{index}[f] = nestedBatch_{index}.Column(f);");
                        Line($"var structValues_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), nestedList_{index}.Count, nestedCols_{index}, ArrowBuffer.Empty);");
                        Line($"var offsets_{index} = new ArrowBuffer.Builder<int>();");
                        Line($"offsets_{index}.Append(0);");
                        Line($"offsets_{index}.Append(nestedList_{index}.Count);");
                        Line($"var {varName} = new ListArray(new ListType(new Field(\"item\", new StructType({typeName}.ArrowSchema.FieldsList), false)), 1, offsets_{index}.Build(), structValues_{index}, ArrowBuffer.Empty);");
                        break;
                    }
            }
        }

        private void EmitListValuePopulation(TypeInfo elemType, string builderVar, string access, int index, int depth = 0)
        {
            switch (elemType.Kind)
            {
                case ArrowTypeKind.String:
                    {
                        var bt = GetArrayBuilderType(elemType);
                        Line($"foreach (var item_{index} in {access}) (({bt}){builderVar}.ValueBuilder).Append(item_{index});");
                        break;
                    }
                case ArrowTypeKind.Byte:
                    Line($"foreach (var item_{index} in {access}) ((UInt8Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.SByte:
                    Line($"foreach (var item_{index} in {access}) ((Int8Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Int16:
                    Line($"foreach (var item_{index} in {access}) ((Int16Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.UInt16:
                    Line($"foreach (var item_{index} in {access}) ((UInt16Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Int32:
                    Line($"foreach (var item_{index} in {access}) ((Int32Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.UInt32:
                    Line($"foreach (var item_{index} in {access}) ((UInt32Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Int64:
                    Line($"foreach (var item_{index} in {access}) ((Int64Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.UInt64:
                    Line($"foreach (var item_{index} in {access}) ((UInt64Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Float:
                    Line($"foreach (var item_{index} in {access}) ((FloatArray.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Double:
                    Line($"foreach (var item_{index} in {access}) ((DoubleArray.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Half:
                    Line($"foreach (var item_{index} in {access}) ((HalfFloatArray.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Bool:
                    Line($"foreach (var item_{index} in {access}) ((BooleanArray.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Decimal:
                    Line($"foreach (var item_{index} in {access}) ((Decimal128Array.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.DateTime:
                    {
                        var hasTz = TypeInfoHasTimezone(elemType);
                        var convert = hasTz ? "ToUtcDateTimeOffset" : "ToWallClockDateTimeOffset";
                        Line($"foreach (var item_{index} in {access}) ((TimestampArray.Builder){builderVar}.ValueBuilder).Append(Apache.Arrow.Serialization.ArrowArrayHelper.{convert}(item_{index}));");
                        break;
                    }
                case ArrowTypeKind.DateTimeOffset:
                    {
                        var hasTz = TypeInfoHasTimezone(elemType);
                        var convert = hasTz ? "ToUtcDateTimeOffset" : "ToWallClockDateTimeOffset";
                        Line($"foreach (var item_{index} in {access}) ((TimestampArray.Builder){builderVar}.ValueBuilder).Append(Apache.Arrow.Serialization.ArrowArrayHelper.{convert}(item_{index}));");
                        break;
                    }
                case ArrowTypeKind.DateOnly:
                    {
                        var arrayType = GetDateArrayBuilderType(elemType);
                        Line($"foreach (var item_{index} in {access}) (({arrayType}){builderVar}.ValueBuilder).Append(item_{index});");
                        break;
                    }
                case ArrowTypeKind.TimeOnly:
                    {
                        var arrayType = GetTimeArrayBuilderType(elemType);
                        Line($"foreach (var item_{index} in {access}) (({arrayType}){builderVar}.ValueBuilder).Append(item_{index});");
                        break;
                    }
                case ArrowTypeKind.TimeSpan:
                    Line($"foreach (var item_{index} in {access}) ((DurationArray.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Guid:
                    Line($"foreach (var item_{index} in {access}) ((GuidArray.Builder){builderVar}.ValueBuilder).Append(item_{index});");
                    break;
                case ArrowTypeKind.Binary:
                    Line($"foreach (var item_{index} in {access}) ((BinaryArray.Builder){builderVar}.ValueBuilder).Append((ReadOnlySpan<byte>)item_{index});");
                    break;
                case ArrowTypeKind.Enum:
                    // For list of enums, we need to build dictionary-encoded values
                    // Simplified: store as string list for now
                    Line($"foreach (var item_{index} in {access}) ((StringArray.Builder){builderVar}.ValueBuilder).Append(item_{index}.ToString());");
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                    {
                        // Nested list: ValueBuilder is itself a ListArray.Builder
                        var innerElemType = elemType.ElementType!;
                        var innerBuilderCast = $"((ListArray.Builder){builderVar}.ValueBuilder)";
                        var loopVar = depth == 0 ? $"inner_{index}" : $"inner_{index}_{depth}";
                        Line($"foreach (var {loopVar} in {access})");
                        Line("{");
                        _indent++;
                        Line($"{innerBuilderCast}.Append();");
                        EmitListValuePopulation(innerElemType, innerBuilderCast, loopVar, index, depth + 1);
                        _indent--;
                        Line("}");
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        // Dict values inside a list: ValueBuilder is a MapArray.Builder
                        var keyType = elemType.KeyType!;
                        var valueType = elemType.ValueType!;
                        var innerMapBuilder = $"((MapArray.Builder){builderVar}.ValueBuilder)";
                        var loopVar = depth == 0 ? $"dictItem_{index}" : $"dictItem_{index}_{depth}";
                        var kvVar = depth == 0 ? $"mkv_{index}" : $"mkv_{index}_{depth}";
                        Line($"foreach (var {loopVar} in {access})");
                        Line("{");
                        _indent++;
                        Line($"{innerMapBuilder}.Append();");
                        Line($"foreach (var {kvVar} in {loopVar})");
                        Line("{");
                        _indent++;
                        EmitMapKeyAppend(keyType, innerMapBuilder, $"{kvVar}.Key", index, depth + 1);
                        EmitMapValueAppend(valueType, innerMapBuilder, $"{kvVar}.Value", index, depth + 1);
                        _indent--;
                        Line("}");
                        _indent--;
                        Line("}");
                        break;
                    }
                default:
                    Line($"// TODO: List element type {elemType.Kind} not yet supported for population");
                    break;
            }
        }

        /// <summary>
        /// Returns true for value types that cannot use MapArray.Builder because
        /// ArrowArrayBuilderFactory.Build() does not support them.
        /// </summary>
        private static bool NeedsManualMapBuilder(TypeInfo valueType)
        {
            return valueType.Kind == ArrowTypeKind.Guid
                || valueType.Kind == ArrowTypeKind.Enum
                || valueType.Kind == ArrowTypeKind.NestedRecord;
        }

        private void EmitMapArrayBuilder(PropertyModel prop, int index, string varName, string access)
        {
            var keyType = prop.Type.KeyType!;
            var valueType = prop.Type.ValueType!;

            if (NeedsManualMapBuilder(valueType))
            {
                EmitManualMapArray(prop, index, varName, access);
                return;
            }

            var mapType = GetArrowTypeExpression(prop.Type);
            Line($"var mb_{index} = new MapArray.Builder({mapType});");
            Line($"mb_{index}.Append();");
            Line($"foreach (var kv_{index} in {access})");
            Line("{");
            _indent++;
            EmitMapKeyAppend(keyType, $"mb_{index}", $"kv_{index}.Key", index);
            EmitMapValueAppend(valueType, $"mb_{index}", $"kv_{index}.Value", index);
            _indent--;
            Line("}");
            Line($"var {varName} = mb_{index}.Build();");
        }

        /// <summary>
        /// Builds a single-row MapArray manually for value types unsupported by ArrowArrayBuilderFactory.
        /// </summary>
        private void EmitManualMapArray(PropertyModel prop, int index, string varName, string access)
        {
            var keyType = prop.Type.KeyType!;
            var valueType = prop.Type.ValueType!;
            var mapType = GetArrowTypeExpression(prop.Type);

            // Build keys
            var keyBuilderType = GetArrayBuilderType(keyType);
            Line($"var mk_{index} = new {keyBuilderType}();");
            Line($"foreach (var kv_{index} in {access}) mk_{index}.Append(kv_{index}.Key);");
            Line($"var mkArr_{index} = mk_{index}.Build();");

            // Build values based on type
            switch (valueType.Kind)
            {
                case ArrowTypeKind.Guid:
                    Line($"var mvItems_{index} = new System.Collections.Generic.List<System.Guid>();");
                    Line($"foreach (var kv_{index}v in {access}) mvItems_{index}.Add(kv_{index}v.Value);");
                    Line($"var mvArr_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray(mvItems_{index});");
                    break;
                case ArrowTypeKind.Enum:
                    {
                        Line($"var mvDict_{index} = new StringArray.Builder();");
                        Line($"var mvIdx_{index} = new Int16Array.Builder();");
                        Line($"var mvMap_{index} = new System.Collections.Generic.Dictionary<string, short>();");
                        Line($"foreach (var kv_{index}v in {access})");
                        Line("{");
                        _indent++;
                        Line($"var es_{index} = kv_{index}v.Value.ToString();");
                        Line($"if (!mvMap_{index}.TryGetValue(es_{index}, out var ei_{index})) {{ ei_{index} = (short)mvMap_{index}.Count; mvMap_{index}[es_{index}] = ei_{index}; }}");
                        Line($"mvIdx_{index}.Append(ei_{index});");
                        _indent--;
                        Line("}");
                        Line($"foreach (var kv in mvMap_{index}.OrderBy(kv => kv.Value)) mvDict_{index}.Append(kv.Key);");
                        Line($"var mvArr_{index} = new DictionaryArray(new DictionaryType(Int16Type.Default, StringType.Default, false), mvIdx_{index}.Build(), mvDict_{index}.Build());");
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        var typeName = valueType.FullTypeName;
                        Line($"var mvItems_{index} = new System.Collections.Generic.List<{typeName}>();");
                        Line($"foreach (var kv_{index}v in {access}) mvItems_{index}.Add(kv_{index}v.Value);");
                        Line($"IArrowArray mvArr_{index};");
                        Line($"if (mvItems_{index}.Count > 0)");
                        Line("{");
                        _indent++;
                        Line($"var nb_{index} = {typeName}.ToRecordBatch((IReadOnlyList<{typeName}>)mvItems_{index});");
                        Line($"var ca_{index} = new IArrowArray[nb_{index}.ColumnCount];");
                        Line($"for (int f = 0; f < nb_{index}.ColumnCount; f++) ca_{index}[f] = nb_{index}.Column(f);");
                        Line($"mvArr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), mvItems_{index}.Count, ca_{index}, ArrowBuffer.Empty);");
                        _indent--;
                        Line("}");
                        Line($"else mvArr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), 0, System.Array.Empty<IArrowArray>(), ArrowBuffer.Empty);");
                        break;
                    }
            }

            // Construct MapArray manually: offsets, StructArray(keys, values), then MapArray
            Line($"var mOffsets_{index} = new ArrowBuffer.Builder<int>();");
            Line($"mOffsets_{index}.Append(0);");
            Line($"mOffsets_{index}.Append(mkArr_{index}.Length);");
            Line($"var mEntries_{index} = new StructArray(((MapType){mapType}).KeyValueType, mkArr_{index}.Length, new IArrowArray[] {{ mkArr_{index}, mvArr_{index} }}, ArrowBuffer.Empty);");
            Line($"var {varName} = new MapArray({mapType}, 1, mOffsets_{index}.Build(), mEntries_{index}, ArrowBuffer.Empty);");
        }

        private void EmitMapKeyAppend(TypeInfo keyType, string builderVar, string access, int index, int depth = 0)
        {
            var builderType = GetArrayBuilderType(keyType);
            var appendExpr = GetMapAppendExpression(keyType, builderType, $"{builderVar}.KeyBuilder", access);
            Line(appendExpr);
        }

        private void EmitMapValueAppend(TypeInfo valueType, string builderVar, string access, int index, int depth = 0)
        {
            switch (valueType.Kind)
            {
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        // List value in map: ValueBuilder is a ListArray.Builder
                        var innerListBuilder = $"((ListArray.Builder){builderVar}.ValueBuilder)";
                        Line($"{innerListBuilder}.Append();");
                        EmitListValuePopulation(valueType.ElementType!, innerListBuilder, access, index, depth + 1);
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        // Nested map value: ValueBuilder is a MapArray.Builder
                        var innerMapBuilder = $"((MapArray.Builder){builderVar}.ValueBuilder)";
                        var innerKvVar = depth == 0 ? $"mvkv_{index}" : $"mvkv_{index}_{depth}";
                        Line($"{innerMapBuilder}.Append();");
                        Line($"foreach (var {innerKvVar} in {access})");
                        Line("{");
                        _indent++;
                        EmitMapKeyAppend(valueType.KeyType!, innerMapBuilder, $"{innerKvVar}.Key", index, depth + 1);
                        EmitMapValueAppend(valueType.ValueType!, innerMapBuilder, $"{innerKvVar}.Value", index, depth + 1);
                        _indent--;
                        Line("}");
                        break;
                    }
                default:
                    {
                        var builderType = GetArrayBuilderType(valueType);
                        var appendExpr = GetMapAppendExpression(valueType, builderType, $"{builderVar}.ValueBuilder", access);
                        Line(appendExpr);
                        break;
                    }
            }
        }

        private string GetMapAppendExpression(TypeInfo type, string builderType, string builderAccess, string valueAccess)
        {
            switch (type.Kind)
            {
                case ArrowTypeKind.DateTime:
                case ArrowTypeKind.DateTimeOffset:
                    {
                        var convert = TypeInfoHasTimezone(type) ? "ToUtcDateTimeOffset" : "ToWallClockDateTimeOffset";
                        return $"(({builderType}){builderAccess}).Append(Apache.Arrow.Serialization.ArrowArrayHelper.{convert}({valueAccess}));";
                    }
                case ArrowTypeKind.Binary:
                    return $"(({builderType}){builderAccess}).Append((ReadOnlySpan<byte>){valueAccess});";
                case ArrowTypeKind.Enum:
                    return $"(({builderType}){builderAccess}).Append({valueAccess}.ToString());";
                default:
                    return $"(({builderType}){builderAccess}).Append({valueAccess});";
            }
        }

        private void EmitNestedRecordSerializer(PropertyModel prop, int index, string varName, string access)
        {
            var typeName = prop.Type.FullTypeName;

            if (prop.IsNullable)
            {
                Line($"IArrowArray {varName};");
                Line($"if ({access} is {{ }} nv_{index})");
                Line("{");
                _indent++;
                Line($"var nestedBatch_{index} = {typeName}.ToRecordBatch(nv_{index});");
                Line($"var nestedArrays_{index} = new IArrowArray[nestedBatch_{index}.ColumnCount];");
                Line($"for (int i = 0; i < nestedBatch_{index}.ColumnCount; i++) nestedArrays_{index}[i] = nestedBatch_{index}.Column(i);");
                Line($"{varName} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), 1, nestedArrays_{index}, ArrowBuffer.Empty);");
                _indent--;
                Line("}");
                Line("else");
                Line("{");
                _indent++;
                Line($"var nullArrays_{index} = new IArrowArray[{typeName}.ArrowSchema.FieldsList.Count];");
                Line($"for (int i = 0; i < nullArrays_{index}.Length; i++)");
                Line($"    nullArrays_{index}[i] = Apache.Arrow.Serialization.ArrowArrayHelper.BuildNullArray({typeName}.ArrowSchema.FieldsList[i].DataType, 1);");
                Line($"var nullBitmap_{index} = new ArrowBuffer.BitmapBuilder();");
                Line($"nullBitmap_{index}.Append(false);");
                Line($"{varName} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), 1, nullArrays_{index}, nullBitmap_{index}.Build(), 1);");
                _indent--;
                Line("}");
            }
            else
            {
                Line($"var nestedBatch_{index} = {typeName}.ToRecordBatch({access});");
                Line($"var nestedArrays_{index} = new IArrowArray[nestedBatch_{index}.ColumnCount];");
                Line($"for (int i = 0; i < nestedBatch_{index}.ColumnCount; i++) nestedArrays_{index}[i] = nestedBatch_{index}.Column(i);");
                Line($"var {varName} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), 1, nestedArrays_{index}, ArrowBuffer.Empty);");
            }
        }

        private void EmitDeserialize()
        {
            var newKw = _model.HasArrowSerializableBase ? "new " : "";
            Line($"public static {newKw}{_model.TypeName} FromRecordBatch(RecordBatch batch)");
            Line("{");
            _indent++;

            Line("if (batch.Length == 0)");
            Line("    throw new InvalidOperationException(\"Cannot deserialize from empty RecordBatch.\");");
            Line("if (batch.Length > 1)");
            Line("    throw new InvalidOperationException($\"Expected single-row RecordBatch, got {batch.Length} rows.\");");
            Line();

            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitDeserializeProperty(prop, i);
            }

            Line();
            if (_model.HasSerializationCallback)
            {
                EmitObjectConstruction("var result = ", ";");
                Line("((Apache.Arrow.Serialization.IArrowSerializationCallback)result).OnAfterDeserialize();");
                Line("return result;");
            }
            else
            {
                EmitObjectConstruction("return ", ";");
            }

            _indent--;
            Line("}");
        }

        /// <summary>
        /// Emits either constructor call or object initializer to create an instance.
        /// Uses constructor when ConstructorParams is set (field-based types), otherwise object initializer.
        /// </summary>
        private void EmitObjectConstruction(string prefix, string suffix)
        {
            if (_model.ConstructorParams != null)
            {
                // Constructor-based: new TypeName(paramName: prop_N, ...)
                var propByName = new Dictionary<string, int>(StringComparer.OrdinalIgnoreCase);
                for (int i = 0; i < _model.Properties.Count; i++)
                    propByName[_model.Properties[i].PropertyName] = i;

                var args = new List<string>();
                foreach (var param in _model.ConstructorParams)
                {
                    if (propByName.TryGetValue(param.Name, out int idx))
                        args.Add($"{param.Name}: prop_{idx}");
                    else if (param.HasDefaultValue)
                        continue; // use default
                }

                Line($"{prefix}new {_model.TypeName}({string.Join(", ", args)}){suffix}");
            }
            else
            {
                // Object initializer: new TypeName { Prop = prop_N, ... }
                Line($"{prefix}new {_model.TypeName}");
                Line("{");
                _indent++;
                for (int i = 0; i < _model.Properties.Count; i++)
                {
                    var prop = _model.Properties[i];
                    var comma = i < _model.Properties.Count - 1 ? "," : "";
                    Line($"{prop.PropertyName} = prop_{i}{comma}");
                }
                _indent--;
                Line($"}}{suffix}");
            }
        }

        private void EmitDeserializeProperty(PropertyModel prop, int index)
        {
            var colAccess = $"batch.Column(\"{prop.FieldName}\")";

            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.String:
                    {
                        var si = GetStringArrayInfo(prop);
                        if (prop.IsNullable)
                            Line($"var prop_{index} = (({colAccess}) is {si.ArrayType} sa_{index} && !sa_{index}.IsNull(0)) ? sa_{index}.GetString(0) : null;");
                        else
                            Line($"var prop_{index} = (({si.ArrayType}){colAccess}).GetString(0)!;");
                        break;
                    }
                case ArrowTypeKind.Bool:
                    {
                        var bl = GetBoolArrayInfo(prop);
                        EmitPrimitiveDeserialize(prop, index, colAccess, bl.ArrayType, "GetValue");
                        break;
                    }
                case ArrowTypeKind.Byte:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "UInt8Array", "GetValue");
                    break;
                case ArrowTypeKind.SByte:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "Int8Array", "GetValue");
                    break;
                case ArrowTypeKind.Int16:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "Int16Array", "GetValue");
                    break;
                case ArrowTypeKind.UInt16:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "UInt16Array", "GetValue");
                    break;
                case ArrowTypeKind.Int32:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "Int32Array", "GetValue");
                    break;
                case ArrowTypeKind.UInt32:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "UInt32Array", "GetValue");
                    break;
                case ArrowTypeKind.Int64:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "Int64Array", "GetValue");
                    break;
                case ArrowTypeKind.UInt64:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "UInt64Array", "GetValue");
                    break;
                case ArrowTypeKind.Float:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "FloatArray", "GetValue");
                    break;
                case ArrowTypeKind.Double:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "DoubleArray", "GetValue");
                    break;
                case ArrowTypeKind.Binary:
                    {
                        var bi = GetBinaryArrayInfo(prop);
                        if (IsReadOnlyMemoryByte(prop))
                            Line($"ReadOnlyMemory<byte> prop_{index} = (({bi.ArrayType}){colAccess}).GetBytes(0).ToArray();");
                        else
                            Line($"var prop_{index} = (({bi.ArrayType}){colAccess}).GetBytes(0).ToArray();");
                        break;
                    }
                case ArrowTypeKind.DateTime:
                    {
                        var dtProp = TimestampHasTimezone(prop) ? "UtcDateTime" : "DateTime";
                        if (prop.IsNullable)
                            Line($"var prop_{index} = ((TimestampArray){colAccess}).IsNull(0) ? (System.DateTime?)null : ((TimestampArray){colAccess}).GetTimestamp(0)!.Value.{dtProp};");
                        else
                            Line($"var prop_{index} = ((TimestampArray){colAccess}).GetTimestamp(0)!.Value.{dtProp};");
                        break;
                    }
                case ArrowTypeKind.DateTimeOffset:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = ((TimestampArray){colAccess}).IsNull(0) ? (System.DateTimeOffset?)null : ((TimestampArray){colAccess}).GetTimestamp(0)!.Value;");
                    else
                        Line($"var prop_{index} = ((TimestampArray){colAccess}).GetTimestamp(0)!.Value;");
                    break;
                case ArrowTypeKind.DateOnly:
                    {
                        var di = GetDateArrayInfo(prop);
                        if (prop.IsNullable)
                            Line($"var prop_{index} = (({di.ArrayType}){colAccess}).IsNull(0) ? (System.DateOnly?)null : (({di.ArrayType}){colAccess}).GetDateOnly(0);");
                        else
                            Line($"var prop_{index} = (({di.ArrayType}){colAccess}).GetDateOnly(0)!.Value;");
                        break;
                    }
                case ArrowTypeKind.TimeOnly:
                    {
                        var ti = GetTimeArrayInfo(prop);
                        if (prop.IsNullable)
                            Line($"var prop_{index} = (({ti.ArrayType}){colAccess}).GetTime(0);");
                        else
                            Line($"var prop_{index} = (({ti.ArrayType}){colAccess}).GetTime(0)!.Value;");
                        break;
                    }
                case ArrowTypeKind.TimeSpan:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = ((DurationArray){colAccess}).IsNull(0) ? (System.TimeSpan?)null : Apache.Arrow.Serialization.ArrowArrayHelper.ReadDuration((DurationArray){colAccess}, 0);");
                    else
                        Line($"var prop_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.ReadDuration((DurationArray){colAccess}, 0);");
                    break;
                case ArrowTypeKind.Decimal:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = ((Decimal128Array){colAccess}).IsNull(0) ? (decimal?)null : ((Decimal128Array){colAccess}).GetValue(0).Value;");
                    else
                        Line($"var prop_{index} = ((Decimal128Array){colAccess}).GetValue(0).Value;");
                    break;
                case ArrowTypeKind.Guid:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = ((GuidArray){colAccess}).GetGuid(0);");
                    else
                        Line($"var prop_{index} = ((GuidArray){colAccess}).GetGuid(0)!.Value;");
                    break;
                case ArrowTypeKind.Half:
                    EmitPrimitiveDeserialize(prop, index, colAccess, "HalfFloatArray", "GetValue");
                    break;
                case ArrowTypeKind.Enum:
                    EmitEnumDeserialize(prop, index, colAccess);
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                    EmitListDeserialize(prop, index, colAccess);
                    break;
                case ArrowTypeKind.Set:
                    EmitSetDeserialize(prop, index, colAccess);
                    break;
                case ArrowTypeKind.Dictionary:
                    EmitDictDeserialize(prop, index, colAccess);
                    break;
                case ArrowTypeKind.NestedRecord:
                    EmitNestedRecordDeserialize(prop, index, colAccess);
                    break;
                case ArrowTypeKind.Custom:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = {colAccess}.IsNull(0) ? ({prop.Type.FullTypeName}?)null : new {prop.ConverterTypeName}().FromArray({colAccess}, 0);");
                    else
                        Line($"var prop_{index} = new {prop.ConverterTypeName}().FromArray({colAccess}, 0);");
                    break;
                default:
                    Line($"object? prop_{index} = null; // TODO: Unsupported type {prop.Type.Kind}");
                    break;
            }
        }

        private void EmitPrimitiveDeserialize(PropertyModel prop, int index, string colAccess, string arrayType, string getMethod)
        {
            if (prop.IsNullable)
                Line($"var prop_{index} = (({arrayType}){colAccess}).IsNull(0) ? null : ({prop.Type.FullTypeName}?)(({arrayType}){colAccess}).{getMethod}(0);");
            else
                Line($"var prop_{index} = (({arrayType}){colAccess}).{getMethod}(0).Value;");
        }

        private void EmitEnumDeserialize(PropertyModel prop, int index, string colAccess)
        {
            var enumType = prop.Type.FullTypeName;
            if (prop.IsNullable)
            {
                Line($"var dictArr_{index} = (DictionaryArray){colAccess};");
                Line($"{enumType}? prop_{index} = dictArr_{index}.IsNull(0) ? null : ArrowArrayHelper.ParseEnum<{enumType}>(((StringArray)dictArr_{index}.Dictionary).GetString((int)((Int16Array)dictArr_{index}.Indices).GetValue(0).Value)!);");
            }
            else
            {
                Line($"var dictArr_{index} = (DictionaryArray){colAccess};");
                Line($"var prop_{index} = ArrowArrayHelper.ParseEnum<{enumType}>(((StringArray)dictArr_{index}.Dictionary).GetString((int)((Int16Array)dictArr_{index}.Indices).GetValue(0).Value)!);");
            }
        }

        private void EmitListDeserialize(PropertyModel prop, int index, string colAccess)
        {
            var elemType = prop.Type.ElementType!;
            var listArr = $"listArr_{index}";
            Line($"var {listArr} = (ListArray){colAccess};");

            if (prop.IsNullable)
            {
                var collTypeName = prop.Type.Kind == ArrowTypeKind.Array
                    ? $"{elemType.FullTypeName}[]"
                    : $"System.Collections.Generic.List<{elemType.FullTypeName}>";
                Line($"{collTypeName}? prop_{index};");
                Line($"if ({listArr}.IsNull(0))");
                Line($"    prop_{index} = null;");
                Line("else");
                Line("{");
                _indent++;
                EmitListDeserializeInner(prop, elemType, index, listArr, isSet: false, assignTarget: $"prop_{index}");
                _indent--;
                Line("}");
            }
            else
            {
                EmitListDeserializeInner(prop, elemType, index, listArr, isSet: false, assignTarget: null);
            }
        }

        /// <summary>
        /// Emits the inner list deserialization logic (non-null path).
        /// If assignTarget is null, declares <c>var prop_{index}</c>; otherwise assigns to assignTarget.
        /// </summary>
        private void EmitListDeserializeInner(PropertyModel prop, TypeInfo elemType, int index, string listArr, bool isSet, string? assignTarget)
        {
            var declPrefix = assignTarget != null ? $"{assignTarget}" : $"var prop_{index}";
            if (NeedsManualListDeserialize(elemType))
            {
                EmitManualListDeserialize(prop, elemType, index, listArr, isSet, assignTarget);
            }
            else
            {
                Line($"var {listArr}_values = {listArr}.GetSlicedValues(0);");
                var elemReader = GetElementReader(elemType, $"{listArr}_values");
                if (isSet)
                    Line($"{declPrefix} = new System.Collections.Generic.HashSet<{elemType.FullTypeName}>({elemReader});");
                else if (prop.Type.Kind == ArrowTypeKind.Array)
                    Line($"{declPrefix} = {elemReader}.ToArray();");
                else
                    Line($"{declPrefix} = new System.Collections.Generic.List<{elemType.FullTypeName}>({elemReader});");
            }
        }

        private void EmitSetDeserialize(PropertyModel prop, int index, string colAccess)
        {
            var elemType = prop.Type.ElementType!;
            var listArr = $"listArr_{index}";
            Line($"var {listArr} = (ListArray){colAccess};");

            if (prop.IsNullable)
            {
                var collTypeName = $"System.Collections.Generic.HashSet<{elemType.FullTypeName}>";
                Line($"{collTypeName}? prop_{index};");
                Line($"if ({listArr}.IsNull(0))");
                Line($"    prop_{index} = null;");
                Line("else");
                Line("{");
                _indent++;
                EmitListDeserializeInner(prop, elemType, index, listArr, isSet: true, assignTarget: $"prop_{index}");
                _indent--;
                Line("}");
            }
            else
            {
                EmitListDeserializeInner(prop, elemType, index, listArr, isSet: true, assignTarget: null);
            }
        }

        /// <summary>
        /// Emits deserialization for single-row list/set with extension type elements (e.g. Guid)
        /// that cannot use GetSlicedValues (which fails for ExtensionArray).
        /// </summary>
        private void EmitManualListDeserialize(PropertyModel prop, TypeInfo elemType, int index, string listArr, bool isSet, string? assignTarget = null)
        {
            // When assignTarget is set, assign to it; otherwise declare var prop_{index}
            string PropDecl(string expr) => assignTarget != null ? $"{assignTarget} = {expr};" : $"var prop_{index} = {expr};";

            // Read offsets and access the full values array directly
            Line($"var {listArr}_start = {listArr}.ValueOffsets[0];");
            Line($"var {listArr}_len = {listArr}.GetValueLength(0);");
            switch (elemType.Kind)
            {
                case ArrowTypeKind.Guid:
                    {
                        Line($"var {listArr}_allValues = (GuidArray){listArr}.Values;");
                        var readExpr = $"Enumerable.Range({listArr}_start, {listArr}_len).Select(i => {listArr}_allValues.GetGuid(i)!.Value)";
                        if (isSet)
                            Line(PropDecl($"new System.Collections.Generic.HashSet<{elemType.FullTypeName}>({readExpr})"));
                        else if (prop.Type.Kind == ArrowTypeKind.Array)
                            Line(PropDecl($"{readExpr}.ToArray()"));
                        else
                            Line(PropDecl($"new System.Collections.Generic.List<{elemType.FullTypeName}>({readExpr})"));
                        break;
                    }
                case ArrowTypeKind.Enum:
                    {
                        // Enum list elements are dictionary-encoded: Dictionary(Int16, Utf8)
                        Line($"var {listArr}_dictArr = (DictionaryArray){listArr}.Values;");
                        Line($"var {listArr}_dictValues = (StringArray){listArr}_dictArr.Dictionary;");
                        var readExpr = $"Enumerable.Range({listArr}_start, {listArr}_len).Select(i => ArrowArrayHelper.ParseEnum<{elemType.FullTypeName}>({listArr}_dictValues.GetString(((Int16Array){listArr}_dictArr.Indices).GetValue(i)!.Value)!))";
                        if (isSet)
                            Line(PropDecl($"new System.Collections.Generic.HashSet<{elemType.FullTypeName}>({readExpr})"));
                        else if (prop.Type.Kind == ArrowTypeKind.Array)
                            Line(PropDecl($"{readExpr}.ToArray()"));
                        else
                            Line(PropDecl($"new System.Collections.Generic.List<{elemType.FullTypeName}>({readExpr})"));
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        // Nested record list elements: read from StructArray via child's FromRecordBatch
                        var typeName = elemType.FullTypeName;
                        Line($"var {listArr}_struct = (StructArray){listArr}.Values;");
                        Line($"var {listArr}_result = new System.Collections.Generic.List<{typeName}>({listArr}_len);");
                        Line($"for (int i = {listArr}_start; i < {listArr}_start + {listArr}_len; i++)");
                        Line("{");
                        _indent++;
                        Line($"var childArrays_{index} = new IArrowArray[{listArr}_struct.Fields.Count];");
                        Line($"for (int f = 0; f < {listArr}_struct.Fields.Count; f++) childArrays_{index}[f] = {listArr}_struct.Fields[f].Data.Slice(i, 1) is var sd_{index} ? ArrowArrayFactory.BuildArray(sd_{index}) : throw new System.InvalidOperationException();");
                        Line($"var childBatch_{index} = new RecordBatch({typeName}.ArrowSchema, childArrays_{index}, 1);");
                        Line($"{listArr}_result.Add({typeName}.FromRecordBatch(childBatch_{index}));");
                        _indent--;
                        Line("}");
                        if (isSet)
                            Line(PropDecl($"new System.Collections.Generic.HashSet<{typeName}>({listArr}_result)"));
                        else if (prop.Type.Kind == ArrowTypeKind.Array)
                            Line(PropDecl($"{listArr}_result.ToArray()"));
                        else
                            Line(PropDecl($"{listArr}_result"));
                        break;
                    }
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                    {
                        // Nested list elements: read inner lists from ListArray values
                        var innerElemType = elemType.ElementType!;
                        var innerTypeName = elemType.FullTypeName;
                        Line($"var {listArr}_innerArr = (ListArray){listArr}.Values;");
                        Line($"var {listArr}_result = new System.Collections.Generic.List<{innerTypeName}>({listArr}_len);");
                        Line($"for (int i = {listArr}_start; i < {listArr}_start + {listArr}_len; i++)");
                        Line("{");
                        _indent++;
                        if (innerElemType.Kind == ArrowTypeKind.Dictionary)
                        {
                            // Dict inner elements: read each dict from inner list's MapArray values
                            var keyType = innerElemType.KeyType!;
                            var valType = innerElemType.ValueType!;
                            var dictTypeName = $"System.Collections.Generic.Dictionary<{keyType.FullTypeName}, {valType.FullTypeName}>";
                            Line($"var {listArr}_mapSlice = (MapArray){listArr}_innerArr.GetSlicedValues(i);");
                            Line($"var {listArr}_innerResult = new System.Collections.Generic.List<{dictTypeName}>({listArr}_mapSlice.Length);");
                            Line($"for (int j_{index} = 0; j_{index} < {listArr}_mapSlice.Length; j_{index}++)");
                            Line("{");
                            _indent++;
                            Line($"var kvs_{index} = (StructArray){listArr}_mapSlice.GetSlicedValues(j_{index});");
                            Line($"var ks_{index} = kvs_{index}.Fields[0];");
                            Line($"var vs_{index} = kvs_{index}.Fields[1];");
                            Line($"var d_{index} = new {dictTypeName}();");
                            Line($"for (int k_{index} = 0; k_{index} < kvs_{index}.Length; k_{index}++)");
                            Line("{");
                            _indent++;
                            var dKeyReader = GetScalarReader(keyType, $"ks_{index}", $"k_{index}");
                            EmitDictValueRead(valType, $"d_{index}", dKeyReader, $"vs_{index}", $"k_{index}", index);
                            _indent--;
                            Line("}");
                            Line($"{listArr}_innerResult.Add(d_{index});");
                            _indent--;
                            Line("}");
                            Line($"{listArr}_result.Add({listArr}_innerResult);");
                        }
                        else
                        {
                            Line($"var {listArr}_innerValues = {listArr}_innerArr.GetSlicedValues(i);");
                            var innerElemReader = GetElementReader(innerElemType, $"{listArr}_innerValues");
                            if (elemType.Kind == ArrowTypeKind.Array)
                                Line($"{listArr}_result.Add({innerElemReader}.ToArray());");
                            else
                                Line($"{listArr}_result.Add(new {innerTypeName}({innerElemReader}));");
                        }
                        _indent--;
                        Line("}");
                        if (isSet)
                            Line(PropDecl($"new System.Collections.Generic.HashSet<{innerTypeName}>({listArr}_result)"));
                        else if (prop.Type.Kind == ArrowTypeKind.Array)
                            Line(PropDecl($"{listArr}_result.ToArray()"));
                        else
                            Line(PropDecl($"{listArr}_result"));
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        // Dict elements inside a list: read each dict from MapArray values
                        var keyType = elemType.KeyType!;
                        var valType = elemType.ValueType!;
                        var dictTypeName = $"System.Collections.Generic.Dictionary<{keyType.FullTypeName}, {valType.FullTypeName}>";
                        Line($"var mapArr_{index} = (MapArray){listArr}.Values;");
                        Line($"var {listArr}_result = new System.Collections.Generic.List<{dictTypeName}>({listArr}_len);");
                        Line($"for (int i = {listArr}_start; i < {listArr}_start + {listArr}_len; i++)");
                        Line("{");
                        _indent++;
                        Line($"var kvs_{index} = (StructArray)mapArr_{index}.GetSlicedValues(i);");
                        Line($"var ks_{index} = kvs_{index}.Fields[0];");
                        Line($"var vs_{index} = kvs_{index}.Fields[1];");
                        Line($"var d_{index} = new {dictTypeName}();");
                        Line($"for (int j_{index} = 0; j_{index} < kvs_{index}.Length; j_{index}++)");
                        Line("{");
                        _indent++;
                        var dKeyReader = GetScalarReader(keyType, $"ks_{index}", $"j_{index}");
                        EmitDictValueRead(valType, $"d_{index}", dKeyReader, $"vs_{index}", $"j_{index}", index);
                        _indent--;
                        Line("}");
                        Line($"{listArr}_result.Add(d_{index});");
                        _indent--;
                        Line("}");
                        if (isSet)
                            Line(PropDecl($"new System.Collections.Generic.HashSet<{dictTypeName}>({listArr}_result)"));
                        else if (prop.Type.Kind == ArrowTypeKind.Array)
                            Line(PropDecl($"{listArr}_result.ToArray()"));
                        else
                            Line(PropDecl($"{listArr}_result"));
                        break;
                    }
            }
        }

        private string GetElementReader(TypeInfo elemType, string valuesVar)
        {
            var stringArrayType = GetArrayReadType(elemType);
            return elemType.Kind switch
            {
                ArrowTypeKind.String => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => (({stringArrayType}){valuesVar}).GetString(i)!)",
                ArrowTypeKind.Byte => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((UInt8Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.SByte => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Int8Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Int16 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Int16Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.UInt16 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((UInt16Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Int32 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Int32Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.UInt32 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((UInt32Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Int64 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Int64Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.UInt64 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((UInt64Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Float => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((FloatArray){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Double => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((DoubleArray){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Half => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((HalfFloatArray){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Bool => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((BooleanArray){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.Decimal => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Decimal128Array){valuesVar}).GetValue(i).Value)",
                ArrowTypeKind.DateTime => TypeInfoHasTimezone(elemType)
                    ? $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((TimestampArray){valuesVar}).GetTimestamp(i)!.Value.UtcDateTime)"
                    : $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((TimestampArray){valuesVar}).GetTimestamp(i)!.Value.DateTime)",
                ArrowTypeKind.DateTimeOffset => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((TimestampArray){valuesVar}).GetTimestamp(i)!.Value)",
                ArrowTypeKind.DateOnly => GetDateArrayReadType(elemType) == "Date64Array"
                    ? $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Date64Array){valuesVar}).GetDateOnly(i).Value)"
                    : $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Date32Array){valuesVar}).GetDateOnly(i).Value)",
                ArrowTypeKind.TimeOnly => GetTimeArrayReadType(elemType) == "Time32Array"
                    ? $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Time32Array){valuesVar}).GetTime(i).Value)"
                    : $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Time64Array){valuesVar}).GetTime(i).Value)",
                ArrowTypeKind.TimeSpan => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => Apache.Arrow.Serialization.ArrowArrayHelper.ReadDuration((DurationArray){valuesVar}, i))",
                ArrowTypeKind.Guid => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((GuidArray){valuesVar}).GetGuid(i)!.Value)",
                ArrowTypeKind.Binary => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((BinaryArray){valuesVar}).GetBytes(i).ToArray())",
                ArrowTypeKind.Enum => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ArrowArrayHelper.ParseEnum<{elemType.FullTypeName}>(((StringArray){valuesVar}).GetString(i)!))",
                _ => $"System.Array.Empty<{elemType.FullTypeName}>()",
            };
        }

        private void EmitDictDeserialize(PropertyModel prop, int index, string colAccess)
        {
            var keyType = prop.Type.KeyType!;
            var valueType = prop.Type.ValueType!;
            var dictTypeName = $"System.Collections.Generic.Dictionary<{keyType.FullTypeName}, {valueType.FullTypeName}>";

            Line($"var mapArr_{index} = (MapArray){colAccess};");
            if (prop.IsNullable)
            {
                Line($"{dictTypeName}? prop_{index};");
                Line($"if (mapArr_{index}.IsNull(0))");
                Line($"    prop_{index} = null;");
                Line("else");
                Line("{");
                _indent++;
                Line($"prop_{index} = new {dictTypeName}();");
            }
            else
            {
                Line($"var prop_{index} = new {dictTypeName}();");
                Line($"if (!mapArr_{index}.IsNull(0))");
                Line("{");
                _indent++;
            }
            Line($"var kvStruct_{index} = (StructArray)mapArr_{index}.GetSlicedValues(0);");
            Line($"var keys_{index} = kvStruct_{index}.Fields[0];");
            Line($"var vals_{index} = kvStruct_{index}.Fields[1];");
            Line($"for (int i = 0; i < kvStruct_{index}.Length; i++)");
            Line("{");
            _indent++;
            var keyReader = GetScalarReader(keyType, $"keys_{index}", "i");
            EmitDictValueRead(valueType, $"prop_{index}", keyReader, $"vals_{index}", "i", index);
            _indent--;
            Line("}");
            _indent--;
            Line("}");
        }

        /// <summary>
        /// Emits code to read a dict value and assign it to dictVar[keyExpr].
        /// Handles scalar values via GetScalarReader, and recurses for List/Dict values.
        /// </summary>
        private void EmitDictValueRead(TypeInfo valueType, string dictVar, string keyExpr, string valsArrayVar, string indexVar, int propIndex, int depth = 0)
        {
            var d = depth > 0 ? $"_{depth}" : "";
            switch (valueType.Kind)
            {
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        var elemType = valueType.ElementType!;
                        var listValsVar = $"lv_{propIndex}{d}";
                        Line($"var {listValsVar} = ((ListArray){valsArrayVar}).GetSlicedValues({indexVar});");
                        var elemReader = GetElementReader(elemType, listValsVar);
                        Line($"{dictVar}[{keyExpr}] = new System.Collections.Generic.List<{elemType.FullTypeName}>({elemReader});");
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        var innerKeyType = valueType.KeyType!;
                        var innerValType = valueType.ValueType!;
                        var innerKvsVar = $"ikvs_{propIndex}{d}";
                        var innerKeysVar = $"iks_{propIndex}{d}";
                        var innerValsVar = $"ivs_{propIndex}{d}";
                        var innerDictVar = $"id_{propIndex}{d}";
                        var innerIdxVar = $"ii_{propIndex}{d}";
                        Line($"var {innerKvsVar} = (StructArray)((MapArray){valsArrayVar}).GetSlicedValues({indexVar});");
                        Line($"var {innerKeysVar} = {innerKvsVar}.Fields[0];");
                        Line($"var {innerValsVar} = {innerKvsVar}.Fields[1];");
                        Line($"var {innerDictVar} = new System.Collections.Generic.Dictionary<{innerKeyType.FullTypeName}, {innerValType.FullTypeName}>();");
                        Line($"for (int {innerIdxVar} = 0; {innerIdxVar} < {innerKvsVar}.Length; {innerIdxVar}++)");
                        Line("{");
                        _indent++;
                        var innerKeyExpr = GetScalarReader(innerKeyType, innerKeysVar, innerIdxVar);
                        EmitDictValueRead(innerValType, innerDictVar, innerKeyExpr, innerValsVar, innerIdxVar, propIndex, depth + 1);
                        _indent--;
                        Line("}");
                        Line($"{dictVar}[{keyExpr}] = {innerDictVar};");
                        break;
                    }
                case ArrowTypeKind.Enum:
                    {
                        // Enum values in maps are dictionary-encoded: Dictionary(Int16, Utf8)
                        var enumType = valueType.FullTypeName;
                        var dvVar = $"dv_{propIndex}{d}";
                        Line($"var {dvVar} = (DictionaryArray){valsArrayVar};");
                        Line($"{dictVar}[{keyExpr}] = ArrowArrayHelper.ParseEnum<{enumType}>(((StringArray){dvVar}.Dictionary).GetString((int)((Int16Array){dvVar}.Indices).GetValue({indexVar}).Value)!);");
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        var typeName = valueType.FullTypeName;
                        var sVar = $"sv_{propIndex}{d}";
                        Line($"var {sVar} = (StructArray){valsArrayVar};");
                        Line($"var ca_{propIndex}{d} = new IArrowArray[{sVar}.Fields.Count];");
                        Line($"for (int f = 0; f < {sVar}.Fields.Count; f++) ca_{propIndex}{d}[f] = ArrowArrayFactory.BuildArray({sVar}.Fields[f].Data.Slice({indexVar}, 1));");
                        Line($"{dictVar}[{keyExpr}] = {typeName}.FromRecordBatch(new RecordBatch({typeName}.ArrowSchema, ca_{propIndex}{d}, 1));");
                        break;
                    }
                default:
                    {
                        var valueReader = GetScalarReader(valueType, valsArrayVar, indexVar);
                        Line($"{dictVar}[{keyExpr}] = {valueReader};");
                        break;
                    }
            }
        }

        private string GetScalarReader(TypeInfo type, string arrayVar, string indexVar)
        {
            var stringArrayType = GetArrayReadType(type);
            return type.Kind switch
            {
                ArrowTypeKind.String => $"(({stringArrayType}){arrayVar}).GetString({indexVar})!",
                ArrowTypeKind.Byte => $"((UInt8Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.SByte => $"((Int8Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Int16 => $"((Int16Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.UInt16 => $"((UInt16Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Int32 => $"((Int32Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.UInt32 => $"((UInt32Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Int64 => $"((Int64Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.UInt64 => $"((UInt64Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Float => $"((FloatArray){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Double => $"((DoubleArray){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Half => $"((HalfFloatArray){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Bool => $"((BooleanArray){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.Decimal => $"((Decimal128Array){arrayVar}).GetValue({indexVar}).Value",
                ArrowTypeKind.DateTime => TypeInfoHasTimezone(type)
                    ? $"((TimestampArray){arrayVar}).GetTimestamp({indexVar})!.Value.UtcDateTime"
                    : $"((TimestampArray){arrayVar}).GetTimestamp({indexVar})!.Value.DateTime",
                ArrowTypeKind.DateTimeOffset => $"((TimestampArray){arrayVar}).GetTimestamp({indexVar})!.Value",
                ArrowTypeKind.DateOnly => GetDateArrayReadType(type) == "Date64Array"
                    ? $"((Date64Array){arrayVar}).GetDateOnly({indexVar}).Value"
                    : $"((Date32Array){arrayVar}).GetDateOnly({indexVar}).Value",
                ArrowTypeKind.TimeOnly => GetTimeArrayReadType(type) == "Time32Array"
                    ? $"((Time32Array){arrayVar}).GetTime({indexVar}).Value"
                    : $"((Time64Array){arrayVar}).GetTime({indexVar}).Value",
                ArrowTypeKind.TimeSpan => $"Apache.Arrow.Serialization.ArrowArrayHelper.ReadDuration((DurationArray){arrayVar}, {indexVar})",
                ArrowTypeKind.Guid => $"((GuidArray){arrayVar}).GetGuid({indexVar})!.Value",
                ArrowTypeKind.Binary => $"((BinaryArray){arrayVar}).GetBytes({indexVar}).ToArray()",
                ArrowTypeKind.Enum => $"ArrowArrayHelper.ParseEnum<{type.FullTypeName}>(((StringArray){arrayVar}).GetString({indexVar})!)",
                _ => $"default({type.FullTypeName})",
            };
        }

        private void EmitNestedRecordDeserialize(PropertyModel prop, int index, string colAccess)
        {
            var typeName = prop.Type.FullTypeName;

            if (prop.IsNullable)
            {
                Line($"var structArr_{index} = (StructArray){colAccess};");
                Line($"{typeName}? prop_{index};");
                Line($"if (structArr_{index}.IsNull(0))");
                Line("{");
                _indent++;
                Line($"prop_{index} = null;");
                _indent--;
                Line("}");
                Line("else");
                Line("{");
                _indent++;
                Line($"var nestedArrays_{index} = new IArrowArray[structArr_{index}.Fields.Count];");
                Line($"for (int i = 0; i < structArr_{index}.Fields.Count; i++) nestedArrays_{index}[i] = structArr_{index}.Fields[i];");
                Line($"var nestedBatch_{index} = new RecordBatch({typeName}.ArrowSchema, nestedArrays_{index}, 1);");
                Line($"prop_{index} = {typeName}.FromRecordBatch(nestedBatch_{index});");
                _indent--;
                Line("}");
            }
            else
            {
                Line($"var structArr_{index} = (StructArray){colAccess};");
                Line($"var nestedArrays_{index} = new IArrowArray[structArr_{index}.Fields.Count];");
                Line($"for (int i = 0; i < structArr_{index}.Fields.Count; i++) nestedArrays_{index}[i] = structArr_{index}.Fields[i];");
                Line($"var nestedBatch_{index} = new RecordBatch({typeName}.ArrowSchema, nestedArrays_{index}, 1);");
                Line($"var prop_{index} = {typeName}.FromRecordBatch(nestedBatch_{index});");
            }
        }

        private void EmitMultiRowSerialize()
        {
            Line($"public static RecordBatch ToRecordBatch(IReadOnlyList<{_model.TypeName}> values)");
            Line("{");
            _indent++;

            Line("var count = values.Count;");
            Line();

            // Create a builder for each column
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitMultiRowBuilderDecl(prop, i);
            }

            Line();
            Line("for (int row = 0; row < count; row++)");
            Line("{");
            _indent++;
            Line("var item = values[row];");

            if (_model.HasSerializationCallback)
                Line("((Apache.Arrow.Serialization.IArrowSerializationCallback)item).OnBeforeSerialize();");

            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitMultiRowBuilderAppend(prop, i);
            }

            _indent--;
            Line("}");
            Line();

            // Build arrays and return RecordBatch
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitMultiRowBuilderBuild(prop, i);
            }

            Line();
            Line("return new RecordBatch(_arrowSchema, new IArrowArray[]");
            Line("{");
            _indent++;
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var comma = i < _model.Properties.Count - 1 ? "," : "";
                Line($"arr_{i}{comma}");
            }
            _indent--;
            Line("}, count);");

            _indent--;
            Line("}");
        }

        private void EmitMultiRowBuilderDecl(PropertyModel prop, int index)
        {
            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.String:
                    {
                        var si = GetStringArrayInfo(prop);
                        Line($"var bld_{index} = new {si.BuilderType}();");
                        break;
                    }
                case ArrowTypeKind.Bool:
                    {
                        var bl = GetBoolArrayInfo(prop);
                        Line($"var bld_{index} = new {bl.BuilderType}();");
                        break;
                    }
                case ArrowTypeKind.Byte:
                    Line($"var bld_{index} = new UInt8Array.Builder();");
                    break;
                case ArrowTypeKind.SByte:
                    Line($"var bld_{index} = new Int8Array.Builder();");
                    break;
                case ArrowTypeKind.Int16:
                    Line($"var bld_{index} = new Int16Array.Builder();");
                    break;
                case ArrowTypeKind.UInt16:
                    Line($"var bld_{index} = new UInt16Array.Builder();");
                    break;
                case ArrowTypeKind.Int32:
                    Line($"var bld_{index} = new Int32Array.Builder();");
                    break;
                case ArrowTypeKind.UInt32:
                    Line($"var bld_{index} = new UInt32Array.Builder();");
                    break;
                case ArrowTypeKind.Int64:
                    Line($"var bld_{index} = new Int64Array.Builder();");
                    break;
                case ArrowTypeKind.UInt64:
                    Line($"var bld_{index} = new UInt64Array.Builder();");
                    break;
                case ArrowTypeKind.Float:
                    Line($"var bld_{index} = new FloatArray.Builder();");
                    break;
                case ArrowTypeKind.Double:
                    Line($"var bld_{index} = new DoubleArray.Builder();");
                    break;
                case ArrowTypeKind.Binary:
                    {
                        var bi = GetBinaryArrayInfo(prop);
                        Line($"var bld_{index} = new {bi.BuilderType}();");
                        break;
                    }
                case ArrowTypeKind.DateTime:
                case ArrowTypeKind.DateTimeOffset:
                    Line($"var bld_{index} = new TimestampArray.Builder({GetTimestampTypeExpr(prop)});");
                    break;
                case ArrowTypeKind.DateOnly:
                    {
                        var di = GetDateArrayInfo(prop);
                        Line($"var bld_{index} = new {di.BuilderType}();");
                        break;
                    }
                case ArrowTypeKind.TimeOnly:
                    {
                        var ti = GetTimeArrayInfo(prop);
                        Line($"var bld_{index} = new {ti.BuilderType}({ti.TypeExpr});");
                        break;
                    }
                case ArrowTypeKind.TimeSpan:
                case ArrowTypeKind.Decimal:
                    Line($"var bld_{index}_items = new List<{prop.Type.FullTypeName}{(prop.IsNullable ? "?" : "")}>(count);");
                    break;
                case ArrowTypeKind.Guid:
                    Line($"var bld_{index}_items = new List<{prop.Type.FullTypeName}{(prop.IsNullable ? "?" : "")}>(count);");
                    break;
                case ArrowTypeKind.Half:
                    Line($"var bld_{index} = new HalfFloatArray.Builder();");
                    break;
                case ArrowTypeKind.Enum:
                    Line($"var bld_{index}_dict = new StringArray.Builder();");
                    Line($"var bld_{index}_idx = new Int16Array.Builder();");
                    Line($"var bld_{index}_map = new Dictionary<string, short>();");
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        var elemType = prop.Type.ElementType!;
                        if (NeedsManualListBuilder(elemType))
                        {
                            // Collect raw items + offsets for manual ListArray construction
                            Line($"var bld_{index}_items = new List<{elemType.FullTypeName}>();");
                            Line($"var bld_{index}_offsets = new ArrowBuffer.Builder<int>();");
                            if (prop.IsNullable)
                                Line($"var bld_{index}_nulls = new ArrowBuffer.BitmapBuilder();");
                        }
                        else
                        {
                            var arrowElemType = GetArrowTypeExpression(elemType);
                            Line($"var bld_{index} = new ListArray.Builder({arrowElemType});");
                        }
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        var valueType = prop.Type.ValueType!;
                        if (NeedsManualMapBuilder(valueType))
                        {
                            var keyType = prop.Type.KeyType!;
                            var keyBuilderType = GetArrayBuilderType(keyType);
                            Line($"var bld_{index}_keys = new {keyBuilderType}();");
                            switch (valueType.Kind)
                            {
                                case ArrowTypeKind.Guid:
                                    Line($"var bld_{index}_vals = new System.Collections.Generic.List<System.Guid>();");
                                    break;
                                case ArrowTypeKind.Enum:
                                    Line($"var bld_{index}_valDict = new StringArray.Builder();");
                                    Line($"var bld_{index}_valIdx = new Int16Array.Builder();");
                                    Line($"var bld_{index}_valMap = new System.Collections.Generic.Dictionary<string, short>();");
                                    break;
                                case ArrowTypeKind.NestedRecord:
                                    Line($"var bld_{index}_vals = new System.Collections.Generic.List<{valueType.FullTypeName}>();");
                                    break;
                            }
                            Line($"var bld_{index}_offsets = new ArrowBuffer.Builder<int>();");
                            if (prop.IsNullable)
                                Line($"var bld_{index}_nulls = new ArrowBuffer.BitmapBuilder();");
                        }
                        else
                        {
                            var mapType = GetArrowTypeExpression(prop.Type);
                            Line($"var bld_{index} = new MapArray.Builder({mapType});");
                        }
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        var typeName = prop.Type.FullTypeName;
                        Line($"var bld_{index}_items = new List<{typeName}{(prop.IsNullable ? "?" : "")}>(count);");
                        break;
                    }
                case ArrowTypeKind.Custom:
                    Line($"var bld_{index}_items = new List<{prop.Type.FullTypeName}>(count);");
                    break;
                default:
                    Line($"// TODO: Unsupported multi-row builder for {prop.Type.Kind}");
                    break;
            }
        }

        private void EmitMultiRowBuilderAppend(PropertyModel prop, int index)
        {
            var access = $"item.{prop.PropertyName}";

            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.String:
                    Line($"bld_{index}.Append({access});");
                    break;
                case ArrowTypeKind.Bool:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(v_{index}); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append({access});");
                    break;
                case ArrowTypeKind.Byte:
                case ArrowTypeKind.SByte:
                case ArrowTypeKind.Int16:
                case ArrowTypeKind.UInt16:
                case ArrowTypeKind.Int32:
                case ArrowTypeKind.UInt32:
                case ArrowTypeKind.Int64:
                case ArrowTypeKind.UInt64:
                case ArrowTypeKind.Float:
                case ArrowTypeKind.Double:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(v_{index}); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append({access});");
                    break;
                case ArrowTypeKind.Binary:
                    Line($"bld_{index}.Append({BinaryToSpan(prop, access)});");
                    break;
                case ArrowTypeKind.DateTime:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}(v_{index})); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}({access}));");
                    break;
                case ArrowTypeKind.DateTimeOffset:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}(v_{index})); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append(Apache.Arrow.Serialization.ArrowArrayHelper.{TimestampConvertMethod(prop)}({access}));");
                    break;
                case ArrowTypeKind.DateOnly:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(v_{index}); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append({access});");
                    break;
                case ArrowTypeKind.TimeOnly:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(v_{index}); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append({access});");
                    break;
                case ArrowTypeKind.TimeSpan:
                case ArrowTypeKind.Decimal:
                    Line($"bld_{index}_items.Add({access});");
                    break;
                case ArrowTypeKind.Guid:
                    Line($"bld_{index}_items.Add({access});");
                    break;
                case ArrowTypeKind.Half:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} v_{index}) bld_{index}.Append(v_{index}); else bld_{index}.AppendNull();");
                    }
                    else
                        Line($"bld_{index}.Append({access});");
                    break;
                case ArrowTypeKind.Enum:
                    if (prop.IsNullable)
                    {
                        Line($"if ({access} is {{ }} ev_{index})");
                        Line("{");
                        _indent++;
                        Line($"var es_{index} = ev_{index}.ToString();");
                        Line($"if (!bld_{index}_map.TryGetValue(es_{index}, out var ei_{index})) {{ ei_{index} = (short)bld_{index}_map.Count; bld_{index}_map[es_{index}] = ei_{index}; }}");
                        Line($"bld_{index}_idx.Append(ei_{index});");
                        _indent--;
                        Line("}");
                        Line($"else bld_{index}_idx.AppendNull();");
                    }
                    else
                    {
                        Line($"var es_{index} = {access}.ToString();");
                        Line($"if (!bld_{index}_map.TryGetValue(es_{index}, out var ei_{index})) {{ ei_{index} = (short)bld_{index}_map.Count; bld_{index}_map[es_{index}] = ei_{index}; }}");
                        Line($"bld_{index}_idx.Append(ei_{index});");
                    }
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        var elemType = prop.Type.ElementType!;
                        if (prop.IsNullable)
                        {
                            Line($"if ({access} != null)");
                            Line("{");
                            _indent++;
                        }
                        if (NeedsManualListBuilder(elemType))
                        {
                            Line($"bld_{index}_offsets.Append(bld_{index}_items.Count);");
                            Line($"bld_{index}_items.AddRange({access});");
                            if (prop.IsNullable) Line($"bld_{index}_nulls.Append(true);");
                        }
                        else
                        {
                            Line($"bld_{index}.Append();");
                            EmitListValuePopulation(elemType, $"bld_{index}", access, index);
                        }
                        if (prop.IsNullable)
                        {
                            _indent--;
                            Line("}");
                            Line("else");
                            Line("{");
                            _indent++;
                            if (NeedsManualListBuilder(elemType))
                            {
                                Line($"bld_{index}_offsets.Append(bld_{index}_items.Count);");
                                Line($"bld_{index}_nulls.Append(false);");
                            }
                            else
                            {
                                Line($"bld_{index}.AppendNull();");
                            }
                            _indent--;
                            Line("}");
                        }
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        var keyType = prop.Type.KeyType!;
                        var valueType = prop.Type.ValueType!;
                        if (NeedsManualMapBuilder(valueType))
                        {
                            if (prop.IsNullable)
                            {
                                Line($"if ({access} != null)");
                                Line("{");
                                _indent++;
                            }
                            Line($"bld_{index}_offsets.Append(bld_{index}_keys.Length);");
                            Line($"foreach (var kv_{index} in {access})");
                            Line("{");
                            _indent++;
                            Line($"bld_{index}_keys.Append(kv_{index}.Key);");
                            switch (valueType.Kind)
                            {
                                case ArrowTypeKind.Guid:
                                    Line($"bld_{index}_vals.Add(kv_{index}.Value);");
                                    break;
                                case ArrowTypeKind.Enum:
                                    {
                                        Line($"var es_{index} = kv_{index}.Value.ToString();");
                                        Line($"if (!bld_{index}_valMap.TryGetValue(es_{index}, out var ei_{index})) {{ ei_{index} = (short)bld_{index}_valMap.Count; bld_{index}_valMap[es_{index}] = ei_{index}; }}");
                                        Line($"bld_{index}_valIdx.Append(ei_{index});");
                                        break;
                                    }
                                case ArrowTypeKind.NestedRecord:
                                    Line($"bld_{index}_vals.Add(kv_{index}.Value);");
                                    break;
                            }
                            _indent--;
                            Line("}");
                            if (prop.IsNullable)
                            {
                                Line($"bld_{index}_nulls.Append(true);");
                                _indent--;
                                Line("}");
                                Line("else");
                                Line("{");
                                _indent++;
                                Line($"bld_{index}_offsets.Append(bld_{index}_keys.Length);");
                                Line($"bld_{index}_nulls.Append(false);");
                                _indent--;
                                Line("}");
                            }
                        }
                        else
                        {
                            if (prop.IsNullable)
                            {
                                Line($"if ({access} != null)");
                                Line("{");
                                _indent++;
                            }
                            Line($"bld_{index}.Append();");
                            Line($"foreach (var kv_{index} in {access})");
                            Line("{");
                            _indent++;
                            EmitMapKeyAppend(keyType, $"bld_{index}", $"kv_{index}.Key", index);
                            EmitMapValueAppend(valueType, $"bld_{index}", $"kv_{index}.Value", index);
                            _indent--;
                            Line("}");
                            if (prop.IsNullable)
                            {
                                _indent--;
                                Line("}");
                                Line($"else bld_{index}.AppendNull();");
                            }
                        }
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    Line($"bld_{index}_items.Add({access});");
                    break;
                case ArrowTypeKind.Custom:
                    Line($"bld_{index}_items.Add({access});");
                    break;
                default:
                    Line($"// TODO: Unsupported multi-row append for {prop.Type.Kind}");
                    break;
            }
        }

        private void EmitMultiRowBuilderBuild(PropertyModel prop, int index)
        {
            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.Enum:
                    {
                        Line($"var dictValues_{index} = new StringArray.Builder();");
                        Line($"var sortedEntries_{index} = bld_{index}_map.OrderBy(kv => kv.Value).Select(kv => kv.Key);");
                        Line($"foreach (var e in sortedEntries_{index}) dictValues_{index}.Append(e);");
                        Line($"var arr_{index} = new DictionaryArray(new DictionaryType(Int16Type.Default, StringType.Default, false), bld_{index}_idx.Build(), dictValues_{index}.Build());");
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        var typeName = prop.Type.FullTypeName;
                        if (prop.IsNullable)
                        {
                            // For nullable nested: substitute nulls with the first non-null item (value doesn't matter for null rows)
                            Line($"IArrowArray arr_{index};");
                            Line("{");
                            _indent++;
                            Line($"var firstNonNull_{index} = bld_{index}_items.FirstOrDefault(x => x != null);");
                            Line($"var nullBitmap_{index} = new ArrowBuffer.BitmapBuilder();");
                            Line($"int nullCount_{index} = 0;");
                            Line($"if (firstNonNull_{index} != null)");
                            Line("{");
                            _indent++;
                            Line($"var resolved_{index} = new List<{typeName}>(count);");
                            Line($"for (int r = 0; r < count; r++)");
                            Line("{");
                            _indent++;
                            Line($"if (bld_{index}_items[r] != null) {{ resolved_{index}.Add(bld_{index}_items[r]!); nullBitmap_{index}.Append(true); }}");
                            Line($"else {{ resolved_{index}.Add(firstNonNull_{index}!); nullBitmap_{index}.Append(false); nullCount_{index}++; }}");
                            _indent--;
                            Line("}");
                            Line($"var nb_{index} = {typeName}.ToRecordBatch((IReadOnlyList<{typeName}>)resolved_{index});");
                            Line($"var ca_{index} = new IArrowArray[nb_{index}.ColumnCount];");
                            Line($"for (int f = 0; f < nb_{index}.ColumnCount; f++) ca_{index}[f] = nb_{index}.Column(f);");
                            Line($"arr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), count, ca_{index}, nullBitmap_{index}.Build(), nullCount_{index});");
                            _indent--;
                            Line("}");
                            Line("else");
                            Line("{");
                            _indent++;
                            Line($"for (int r = 0; r < count; r++) {{ nullBitmap_{index}.Append(false); nullCount_{index}++; }}");
                            Line($"var nc_{index} = new IArrowArray[{typeName}.ArrowSchema.FieldsList.Count];");
                            Line($"for (int f = 0; f < nc_{index}.Length; f++)");
                            Line($"    nc_{index}[f] = Apache.Arrow.Serialization.ArrowArrayHelper.BuildNullArray({typeName}.ArrowSchema.FieldsList[f].DataType, count);");
                            Line($"arr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), count, nc_{index}, nullBitmap_{index}.Build(), nullCount_{index});");
                            _indent--;
                            Line("}");
                            _indent--;
                            Line("}");
                        }
                        else
                        {
                            // Non-nullable: directly use multi-row serializer
                            Line($"var nb_{index} = {typeName}.ToRecordBatch((IReadOnlyList<{typeName}>)bld_{index}_items);");
                            Line($"var ca_{index} = new IArrowArray[nb_{index}.ColumnCount];");
                            Line($"for (int f = 0; f < nb_{index}.ColumnCount; f++) ca_{index}[f] = nb_{index}.Column(f);");
                            Line($"var arr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), count, ca_{index}, ArrowBuffer.Empty);");
                        }
                        break;
                    }
                case ArrowTypeKind.TimeOnly:
                    Line($"var arr_{index} = bld_{index}.Build();");
                    break;
                case ArrowTypeKind.TimeSpan:
                    Line($"var arr_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildDurationArray(bld_{index}_items);");
                    break;
                case ArrowTypeKind.Decimal:
                    Line($"var arr_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildDecimalArray(bld_{index}_items);");
                    break;
                case ArrowTypeKind.Guid:
                    Line($"var arr_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray(bld_{index}_items);");
                    break;
                case ArrowTypeKind.Custom:
                    Line($"var arr_{index} = new {prop.ConverterTypeName}().ToArray(bld_{index}_items);");
                    break;
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        var elemType = prop.Type.ElementType!;
                        if (NeedsManualListBuilder(elemType))
                        {
                            var arrowElemType = GetArrowTypeExpression(elemType);
                            // Finalize the offsets with the total item count
                            Line($"bld_{index}_offsets.Append(bld_{index}_items.Count);");
                            switch (elemType.Kind)
                            {
                                case ArrowTypeKind.Guid:
                                    {
                                        Line($"var guidValues_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray(bld_{index}_items);");
                                        var guidNullBuf = prop.IsNullable ? $"bld_{index}_nulls.Build()" : "ArrowBuffer.Empty";
                                        Line($"var arr_{index} = new ListArray(new ListType(new Field(\"item\", {arrowElemType}, false)), count, bld_{index}_offsets.Build(), guidValues_{index}, {guidNullBuf});");
                                        break;
                                    }
                                case ArrowTypeKind.Enum:
                                    {
                                        // Dictionary-encode enum values: Dictionary(Int16, Utf8)
                                        Line($"var enumDict_{index} = new StringArray.Builder();");
                                        Line($"var enumIdx_{index} = new Int16Array.Builder();");
                                        Line($"var enumMap_{index} = new System.Collections.Generic.Dictionary<string, short>();");
                                        Line($"foreach (var e in bld_{index}_items) {{");
                                        Line($"    var es = e.ToString();");
                                        Line($"    if (!enumMap_{index}.TryGetValue(es, out var ei)) {{ ei = (short)enumMap_{index}.Count; enumMap_{index}[es] = ei; }}");
                                        Line($"    enumIdx_{index}.Append(ei);");
                                        Line($"}}");
                                        Line($"foreach (var kv in enumMap_{index}.OrderBy(kv => kv.Value)) enumDict_{index}.Append(kv.Key);");
                                        Line($"var enumDictArr_{index} = new DictionaryArray(new DictionaryType(Int16Type.Default, StringType.Default, false), enumIdx_{index}.Build(), enumDict_{index}.Build());");
                                        var enumNullBuf = prop.IsNullable ? $"bld_{index}_nulls.Build()" : "ArrowBuffer.Empty";
                                        Line($"var arr_{index} = new ListArray(ArrowSchema.GetFieldByName(\"{prop.FieldName}\").DataType, count, bld_{index}_offsets.Build(), enumDictArr_{index}, {enumNullBuf});");
                                        break;
                                    }
                                case ArrowTypeKind.NestedRecord:
                                    {
                                        var typeName = elemType.FullTypeName;
                                        Line($"IArrowArray structValues_{index};");
                                        Line($"if (bld_{index}_items.Count > 0)");
                                        Line("{");
                                        _indent++;
                                        Line($"var nb_{index} = {typeName}.ToRecordBatch((IReadOnlyList<{typeName}>)bld_{index}_items);");
                                        Line($"var ca_{index} = new IArrowArray[nb_{index}.ColumnCount];");
                                        Line($"for (int f = 0; f < nb_{index}.ColumnCount; f++) ca_{index}[f] = nb_{index}.Column(f);");
                                        Line($"structValues_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), bld_{index}_items.Count, ca_{index}, ArrowBuffer.Empty);");
                                        _indent--;
                                        Line("}");
                                        Line("else");
                                        Line("{");
                                        _indent++;
                                        Line($"structValues_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), 0, System.Array.Empty<IArrowArray>(), ArrowBuffer.Empty);");
                                        _indent--;
                                        Line("}");
                                        var nestedNullBuf = prop.IsNullable ? $"bld_{index}_nulls.Build()" : "ArrowBuffer.Empty";
                                        Line($"var arr_{index} = new ListArray(new ListType(new Field(\"item\", new StructType({typeName}.ArrowSchema.FieldsList), false)), count, bld_{index}_offsets.Build(), structValues_{index}, {nestedNullBuf});");
                                        break;
                                    }
                            }
                        }
                        else
                        {
                            Line($"var arr_{index} = bld_{index}.Build();");
                        }
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        var valueType = prop.Type.ValueType!;
                        if (NeedsManualMapBuilder(valueType))
                        {
                            var mapType = GetArrowTypeExpression(prop.Type);
                            // Finalize offsets
                            Line($"bld_{index}_offsets.Append(bld_{index}_keys.Length);");
                            Line($"var mkArr_{index} = bld_{index}_keys.Build();");

                            // Build value array
                            switch (valueType.Kind)
                            {
                                case ArrowTypeKind.Guid:
                                    Line($"var mvArr_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.BuildGuidArray(bld_{index}_vals);");
                                    break;
                                case ArrowTypeKind.Enum:
                                    {
                                        Line($"foreach (var kv in bld_{index}_valMap.OrderBy(kv => kv.Value)) bld_{index}_valDict.Append(kv.Key);");
                                        Line($"var mvArr_{index} = new DictionaryArray(new DictionaryType(Int16Type.Default, StringType.Default, false), bld_{index}_valIdx.Build(), bld_{index}_valDict.Build());");
                                        break;
                                    }
                                case ArrowTypeKind.NestedRecord:
                                    {
                                        var typeName = valueType.FullTypeName;
                                        Line($"IArrowArray mvArr_{index};");
                                        Line($"if (bld_{index}_vals.Count > 0)");
                                        Line("{");
                                        _indent++;
                                        Line($"var nb_{index} = {typeName}.ToRecordBatch((IReadOnlyList<{typeName}>)bld_{index}_vals);");
                                        Line($"var ca_{index} = new IArrowArray[nb_{index}.ColumnCount];");
                                        Line($"for (int f = 0; f < nb_{index}.ColumnCount; f++) ca_{index}[f] = nb_{index}.Column(f);");
                                        Line($"mvArr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), bld_{index}_vals.Count, ca_{index}, ArrowBuffer.Empty);");
                                        _indent--;
                                        Line("}");
                                        Line($"else mvArr_{index} = new StructArray(new StructType({typeName}.ArrowSchema.FieldsList), 0, System.Array.Empty<IArrowArray>(), ArrowBuffer.Empty);");
                                        break;
                                    }
                            }

                            // Construct MapArray manually
                            var nullBuf = prop.IsNullable ? $"bld_{index}_nulls.Build()" : "ArrowBuffer.Empty";
                            Line($"var mEntries_{index} = new StructArray(((MapType){mapType}).KeyValueType, mkArr_{index}.Length, new IArrowArray[] {{ mkArr_{index}, mvArr_{index} }}, ArrowBuffer.Empty);");
                            Line($"var arr_{index} = new MapArray({mapType}, count, bld_{index}_offsets.Build(), mEntries_{index}, {nullBuf});");
                        }
                        else
                        {
                            Line($"var arr_{index} = bld_{index}.Build();");
                        }
                        break;
                    }
                default:
                    Line($"var arr_{index} = bld_{index}.Build();");
                    break;
            }
        }

        private void EmitMultiRowDeserialize()
        {
            var newKw = _model.HasArrowSerializableBase ? "new " : "";
            Line($"public static {newKw}IReadOnlyList<{_model.TypeName}> ListFromRecordBatch(RecordBatch batch)");
            Line("{");
            _indent++;
            Line("var count = batch.Length;");
            Line($"var result = new List<{_model.TypeName}>(count);");
            Line();

            // Get column references
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitMultiRowColumnRef(prop, i);
            }

            Line();
            Line("for (int row = 0; row < count; row++)");
            Line("{");
            _indent++;

            // Read each property from the column at 'row'
            for (int i = 0; i < _model.Properties.Count; i++)
            {
                var prop = _model.Properties[i];
                EmitMultiRowReadProperty(prop, i);
            }

            // Construct the record
            if (_model.HasSerializationCallback)
            {
                EmitObjectConstruction("var item = ", ";");
                Line("((Apache.Arrow.Serialization.IArrowSerializationCallback)item).OnAfterDeserialize();");
                Line("result.Add(item);");
            }
            else
            {
                EmitObjectConstruction("result.Add(", ");");
            }

            _indent--;
            Line("}");
            Line();
            Line("return result;");

            _indent--;
            Line("}");
        }

        private void EmitMultiRowColumnRef(PropertyModel prop, int index)
        {
            var colAccess = $"batch.Column(\"{prop.FieldName}\")";

            switch (prop.Type.Kind)
            {
                case ArrowTypeKind.String:
                    {
                        var si = GetStringArrayInfo(prop);
                        Line($"var col_{index} = ({si.ArrayType}){colAccess};");
                        break;
                    }
                case ArrowTypeKind.Bool:
                    {
                        var bl = GetBoolArrayInfo(prop);
                        Line($"var col_{index} = ({bl.ArrayType}){colAccess};");
                        break;
                    }
                case ArrowTypeKind.Byte:
                    Line($"var col_{index} = (UInt8Array){colAccess};");
                    break;
                case ArrowTypeKind.SByte:
                    Line($"var col_{index} = (Int8Array){colAccess};");
                    break;
                case ArrowTypeKind.Int16:
                    Line($"var col_{index} = (Int16Array){colAccess};");
                    break;
                case ArrowTypeKind.UInt16:
                    Line($"var col_{index} = (UInt16Array){colAccess};");
                    break;
                case ArrowTypeKind.Int32:
                    Line($"var col_{index} = (Int32Array){colAccess};");
