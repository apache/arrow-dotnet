            TypeKind2.Int16 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Int16Array){valuesVar}).GetValue(i).Value)",
            TypeKind2.UInt16 => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((UInt16Array){valuesVar}).GetValue(i).Value)",
