            TypeKind2.Decimal => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((Decimal128Array){valuesVar}).GetValue(i).Value)",
            TypeKind2.DateTime => TypeInfoHasTimezone(elemType)
                ? $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((TimestampArray){valuesVar}).GetTimestamp(i)!.Value.UtcDateTime)"
                : $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((TimestampArray){valuesVar}).GetTimestamp(i)!.Value.DateTime)",
            TypeKind2.DateTimeOffset => $"Enumerable.Range(0, {valuesVar}.Length).Select(i => ((TimestampArray){valuesVar}).GetTimestamp(i)!.Value)",
