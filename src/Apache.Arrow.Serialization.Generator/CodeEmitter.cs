                    break;
                case ArrowTypeKind.DateTime:
                    {
                        var dtProp = TimestampHasTimezone(prop) ? "UtcDateTime" : "DateTime";
                        if (prop.IsNullable)
                            Line($"var prop_{index} = col_{index}.IsNull(row) ? (System.DateTime?)null : col_{index}.GetTimestamp(row)!.Value.{dtProp};");
