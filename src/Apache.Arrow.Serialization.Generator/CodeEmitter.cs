                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.GetTime(row);");
                    else
                        Line($"var prop_{index} = col_{index}.GetTime(row)!.Value;");
                    break;
                case ArrowTypeKind.TimeSpan:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.IsNull(row) ? (System.TimeSpan?)null : Apache.Arrow.Serialization.ArrowArrayHelper.ReadDuration(col_{index}, row);");
                    else
                        Line($"var prop_{index} = Apache.Arrow.Serialization.ArrowArrayHelper.ReadDuration(col_{index}, row);");
                    break;
                case ArrowTypeKind.Decimal:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.IsNull(row) ? (decimal?)null : ((Decimal128Array)col_{index}).GetValue(row).Value;");
                    else
                        Line($"var prop_{index} = ((Decimal128Array)col_{index}).GetValue(row).Value;");
                    break;
                case ArrowTypeKind.Guid:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.GetGuid(row);");
                    else
                        Line($"var prop_{index} = col_{index}.GetGuid(row)!.Value;");
                    break;
                case ArrowTypeKind.Half:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.IsNull(row) ? (System.Half?)null : col_{index}.GetValue(row).Value;");
                    else
                        Line($"var prop_{index} = col_{index}.GetValue(row).Value;");
                    break;
                case ArrowTypeKind.Enum:
                    {
                        var enumType = prop.Type.FullTypeName;
                        if (prop.IsNullable)
                            Line($"var prop_{index} = col_{index}.IsNull(row) ? ({enumType}?)null : ArrowArrayHelper.ParseEnum<{enumType}>(((StringArray)col_{index}.Dictionary).GetString((int)((Int16Array)col_{index}.Indices).GetValue(row).Value)!);");
                        else
                            Line($"var prop_{index} = ArrowArrayHelper.ParseEnum<{enumType}>(((StringArray)col_{index}.Dictionary).GetString((int)((Int16Array)col_{index}.Indices).GetValue(row).Value)!);");
                        break;
                    }
                case ArrowTypeKind.List:
                case ArrowTypeKind.Array:
                case ArrowTypeKind.Set:
                    {
                        var elemType = prop.Type.ElementType!;
                        // Nullable list: wrap in IsNull check
                        if (prop.IsNullable)
                        {
                            var collTypeName = prop.Type.Kind switch
                            {
                                ArrowTypeKind.Array => $"{elemType.FullTypeName}[]",
                                ArrowTypeKind.Set => $"System.Collections.Generic.HashSet<{elemType.FullTypeName}>",
                                _ => $"System.Collections.Generic.List<{elemType.FullTypeName}>"
                            };
                            Line($"{collTypeName}? prop_{index};");
                            Line($"if (col_{index}.IsNull(row))");
                            Line($"    prop_{index} = null;");
                            Line("else");
                            Line("{");
                            _indent++;
                        }
                        if (NeedsManualListDeserialize(elemType))
                        {
                            // Extension types can't use GetSlicedValues; use offsets + Values directly
                            Line($"var listStart_{index} = col_{index}.ValueOffsets[row];");
                            Line($"var listLen_{index} = col_{index}.GetValueLength(row);");
                            var multiDeclPrefix = prop.IsNullable ? $"prop_{index}" : $"var prop_{index}";
                            switch (elemType.Kind)
                            {
                                case ArrowTypeKind.Guid:
                                    {
                                        Line($"var guidArr_{index} = (GuidArray)col_{index}.Values;");
                                        var readExpr = $"Enumerable.Range(listStart_{index}, listLen_{index}).Select(i => guidArr_{index}.GetGuid(i)!.Value)";
                                        if (prop.Type.Kind == ArrowTypeKind.Array)
                                            Line($"{multiDeclPrefix} = {readExpr}.ToArray();");
                                        else if (prop.Type.Kind == ArrowTypeKind.Set)
                                            Line($"{multiDeclPrefix} = new System.Collections.Generic.HashSet<{elemType.FullTypeName}>({readExpr});");
                                        else
                                            Line($"{multiDeclPrefix} = new System.Collections.Generic.List<{elemType.FullTypeName}>({readExpr});");
                                        break;
                                    }
                                case ArrowTypeKind.Enum:
                                    {
                                        // Dictionary-encoded enum values: Dictionary(Int16, Utf8)
                                        Line($"var enumDictArr_{index} = (DictionaryArray)col_{index}.Values;");
                                        Line($"var enumDictValues_{index} = (StringArray)enumDictArr_{index}.Dictionary;");
                                        var readExpr2 = $"Enumerable.Range(listStart_{index}, listLen_{index}).Select(i => ArrowArrayHelper.ParseEnum<{elemType.FullTypeName}>(enumDictValues_{index}.GetString(((Int16Array)enumDictArr_{index}.Indices).GetValue(i)!.Value)!))";
                                        if (prop.Type.Kind == ArrowTypeKind.Array)
                                            Line($"{multiDeclPrefix} = {readExpr2}.ToArray();");
                                        else if (prop.Type.Kind == ArrowTypeKind.Set)
                                            Line($"{multiDeclPrefix} = new System.Collections.Generic.HashSet<{elemType.FullTypeName}>({readExpr2});");
                                        else
                                            Line($"{multiDeclPrefix} = new System.Collections.Generic.List<{elemType.FullTypeName}>({readExpr2});");
                                        break;
                                    }
                                case ArrowTypeKind.NestedRecord:
                                    {
                                        var typeName = elemType.FullTypeName;
                                        Line($"var structArr_{index} = (StructArray)col_{index}.Values;");
                                        Line($"{multiDeclPrefix} = new System.Collections.Generic.List<{typeName}>(listLen_{index});");
                                        Line($"for (int i = listStart_{index}; i < listStart_{index} + listLen_{index}; i++)");
                                        Line("{");
                                        _indent++;
                                        Line($"var childArrays_{index} = new IArrowArray[structArr_{index}.Fields.Count];");
                                        Line($"for (int f = 0; f < structArr_{index}.Fields.Count; f++) childArrays_{index}[f] = ArrowArrayFactory.BuildArray(structArr_{index}.Fields[f].Data.Slice(i, 1));");
                                        Line($"prop_{index}.Add({typeName}.FromRecordBatch(new RecordBatch({typeName}.ArrowSchema, childArrays_{index}, 1)));");
                                        _indent--;
                                        Line("}");
                                        break;
                                    }
                                case ArrowTypeKind.List:
                                case ArrowTypeKind.Array:
                                    {
                                        // Nested list elements: read inner lists from ListArray values
                                        var innerElemType = elemType.ElementType!;
                                        var innerTypeName = elemType.FullTypeName;
                                        Line($"var innerArr_{index} = (ListArray)col_{index}.Values;");
                                        Line($"{multiDeclPrefix} = new System.Collections.Generic.List<{innerTypeName}>(listLen_{index});");
                                        Line($"for (int i = listStart_{index}; i < listStart_{index} + listLen_{index}; i++)");
                                        Line("{");
                                        _indent++;
                                        if (innerElemType.Kind == ArrowTypeKind.Dictionary)
                                        {
                                            // Dict inner elements: read each dict from inner list's MapArray
                                            var keyType2 = innerElemType.KeyType!;
                                            var valType2 = innerElemType.ValueType!;
                                            var dictTypeName2 = $"System.Collections.Generic.Dictionary<{keyType2.FullTypeName}, {valType2.FullTypeName}>";
                                            Line($"var mapSlice_{index} = (MapArray)innerArr_{index}.GetSlicedValues(i);");
                                            Line($"var innerResult_{index} = new System.Collections.Generic.List<{dictTypeName2}>(mapSlice_{index}.Length);");
                                            Line($"for (int j_{index} = 0; j_{index} < mapSlice_{index}.Length; j_{index}++)");
                                            Line("{");
                                            _indent++;
                                            Line($"var kvs_{index} = (StructArray)mapSlice_{index}.GetSlicedValues(j_{index});");
                                            Line($"var ks_{index} = kvs_{index}.Fields[0];");
                                            Line($"var vs_{index} = kvs_{index}.Fields[1];");
                                            Line($"var d_{index} = new {dictTypeName2}();");
                                            Line($"for (int k_{index} = 0; k_{index} < kvs_{index}.Length; k_{index}++)");
                                            Line("{");
                                            _indent++;
                                            var dKeyReader2 = GetScalarReader(keyType2, $"ks_{index}", $"k_{index}");
                                            EmitDictValueRead(valType2, $"d_{index}", dKeyReader2, $"vs_{index}", $"k_{index}", index);
                                            _indent--;
                                            Line("}");
                                            Line($"innerResult_{index}.Add(d_{index});");
                                            _indent--;
                                            Line("}");
                                            Line($"prop_{index}.Add(innerResult_{index});");
                                        }
                                        else
                                        {
                                            Line($"var innerValues_{index} = innerArr_{index}.GetSlicedValues(i);");
                                            var innerElemReader = GetElementReader(innerElemType, $"innerValues_{index}");
                                            if (elemType.Kind == ArrowTypeKind.Array)
                                                Line($"prop_{index}.Add({innerElemReader}.ToArray());");
                                            else
                                                Line($"prop_{index}.Add(new {innerTypeName}({innerElemReader}));");
                                        }
                                        _indent--;
                                        Line("}");
                                        break;
                                    }
                                case ArrowTypeKind.Dictionary:
                                    {
                                        // Dict elements inside a list: read each dict from MapArray values
                                        var keyType = elemType.KeyType!;
                                        var valType = elemType.ValueType!;
                                        var dictTypeName = $"System.Collections.Generic.Dictionary<{keyType.FullTypeName}, {valType.FullTypeName}>";
                                        Line($"var mapArr_{index} = (MapArray)col_{index}.Values;");
                                        Line($"{multiDeclPrefix} = new System.Collections.Generic.List<{dictTypeName}>(listLen_{index});");
                                        Line($"for (int i = listStart_{index}; i < listStart_{index} + listLen_{index}; i++)");
                                        Line("{");
                                        _indent++;
                                        Line($"var kvs_{index} = (StructArray)mapArr_{index}.GetSlicedValues(i);");
                                        Line($"var ks_{index} = kvs_{index}.Fields[0];");
                                        Line($"var vs_{index} = kvs_{index}.Fields[1];");
                                        Line($"var d_{index} = new {dictTypeName}();");
                                        Line($"for (int j_{index} = 0; j_{index} < kvs_{index}.Length; j_{index}++)");
                                        Line("{");
                                        _indent++;
                                        var dKeyReader = GetScalarReader(keyType, $"ks_{index}", $"j_{index}");
                                        EmitDictValueRead(valType, $"d_{index}", dKeyReader, $"vs_{index}", $"j_{index}", index);
                                        _indent--;
                                        Line("}");
                                        Line($"prop_{index}.Add(d_{index});");
                                        _indent--;
                                        Line("}");
                                        break;
                                    }
                            }
                        }
                        else
                        {
                            var nonManualDeclPrefix = prop.IsNullable ? $"prop_{index}" : $"var prop_{index}";
                            Line($"var listValues_{index} = col_{index}.GetSlicedValues(row);");
                            var elemReader = GetElementReader(elemType, $"listValues_{index}");
                            if (prop.Type.Kind == ArrowTypeKind.Array)
                                Line($"{nonManualDeclPrefix} = {elemReader}.ToArray();");
                            else if (prop.Type.Kind == ArrowTypeKind.Set)
                                Line($"{nonManualDeclPrefix} = new System.Collections.Generic.HashSet<{elemType.FullTypeName}>({elemReader});");
                            else
                                Line($"{nonManualDeclPrefix} = new System.Collections.Generic.List<{elemType.FullTypeName}>({elemReader});");
                        }
                        if (prop.IsNullable)
                        {
                            _indent--;
                            Line("}");
                        }
                        break;
                    }
                case ArrowTypeKind.Dictionary:
                    {
                        var keyType = prop.Type.KeyType!;
                        var valueType = prop.Type.ValueType!;
                        var dictTypeName2 = $"System.Collections.Generic.Dictionary<{keyType.FullTypeName}, {valueType.FullTypeName}>";
                        if (prop.IsNullable)
                        {
                            Line($"{dictTypeName2}? prop_{index};");
                            Line($"if (col_{index}.IsNull(row))");
                            Line($"    prop_{index} = null;");
                            Line("else");
                            Line("{");
                            _indent++;
                            Line($"prop_{index} = new {dictTypeName2}();");
                        }
                        else
                        {
                            Line($"var prop_{index} = new {dictTypeName2}();");
                            Line($"if (!col_{index}.IsNull(row))");
                            Line("{");
                            _indent++;
                        }
                        Line($"var kvStruct_{index} = (StructArray)col_{index}.GetSlicedValues(row);");
                        Line($"var keys_{index} = kvStruct_{index}.Fields[0];");
                        Line($"var vals_{index} = kvStruct_{index}.Fields[1];");
                        Line($"for (int i = 0; i < kvStruct_{index}.Length; i++)");
                        Line("{");
                        _indent++;
                        var keyReader = GetScalarReader(keyType, $"keys_{index}", "i");
                        EmitDictValueRead(valueType, $"prop_{index}", keyReader, $"vals_{index}", "i", index);
                        _indent--;
                        Line("}");
                        _indent--;
                        Line("}");
                        break;
                    }
                case ArrowTypeKind.NestedRecord:
                    {
                        var typeName = prop.Type.FullTypeName;
                        if (prop.IsNullable)
                        {
                            Line($"{typeName}? prop_{index};");
                            Line($"if (col_{index}.IsNull(row))");
                            Line("{");
                            _indent++;
                            Line($"prop_{index} = null;");
                            _indent--;
                            Line("}");
                            Line("else");
                            Line("{");
                            _indent++;
                            EmitMultiRowNestedRead(typeName, index, declareVar: false);
                            _indent--;
                            Line("}");
                        }
                        else
                        {
                            EmitMultiRowNestedRead(typeName, index, declareVar: true);
                        }
                        break;
                    }
                case ArrowTypeKind.Custom:
                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.IsNull(row) ? ({prop.Type.FullTypeName}?)null : new {prop.ConverterTypeName}().FromArray(col_{index}, row);");
                    else
                        Line($"var prop_{index} = new {prop.ConverterTypeName}().FromArray(col_{index}, row);");
                    break;
                default:
                    Line($"object? prop_{index} = null; // TODO: Unsupported type {prop.Type.Kind}");
                    break;
            }
        }

