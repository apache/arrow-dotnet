        /// <summary>
        /// Returns the Arrow type expression for list element types.
        /// Enums inside lists use DictionaryType (matching Python's schema).
        /// </summary>
        private string GetListElementTypeExpression(TypeInfo elemType)
        {
            return GetArrowTypeExpression(elemType);
        }

        private static string ParseArrowTypeOverride(string typeName)
        {
            var lower = typeName.ToLowerInvariant().Trim();
            var original = typeName.Trim();

            // Parameterized types: timestamp[unit, tz], decimal128(precision, scale),
            // time64[unit], time32[unit], duration[unit]
            if (lower.StartsWith("timestamp"))
            {
                var (unit, tz) = ParseTimestampParams(original, "timestamp");
                return $"new TimestampType(TimeUnit.{unit}, \"{tz}\")";
            }
            if (lower.StartsWith("decimal128") || lower.StartsWith("decimal"))
            {
                var (precision, scale) = ParseDecimalParams(lower);
                return $"new Decimal128Type({precision}, {scale})";
            }
            if (lower.StartsWith("time64"))
            {
                var unit = ParseUnitParam(lower, "time64", "Microsecond");
                return $"new Time64Type(TimeUnit.{unit})";
            }
            if (lower.StartsWith("time32"))
            {
                var unit = ParseUnitParam(lower, "time32", "Millisecond");
                return $"new Time32Type(TimeUnit.{unit})";
            }
            if (lower.StartsWith("duration"))
            {
                var unit = ParseUnitParam(lower, "duration", "Microsecond");
                return $"DurationType.{unit}";
            }

            return lower switch
            {
                "int8" => "Int8Type.Default",
                "int16" => "Int16Type.Default",
                "int32" => "Int32Type.Default",
                "int64" => "Int64Type.Default",
                "uint8" => "UInt8Type.Default",
                "uint16" => "UInt16Type.Default",
                "uint32" => "UInt32Type.Default",
                "uint64" => "UInt64Type.Default",
                "float16" or "halffloat" => "HalfFloatType.Default",
                "float32" or "float" => "FloatType.Default",
                "float64" or "double" => "DoubleType.Default",
                "bool" or "boolean" => "BooleanType.Default",
                "string" or "utf8" => "StringType.Default",
                "binary" => "BinaryType.Default",
                "date32" => "Date32Type.Default",
                "date64" => "Date64Type.Default",
                "string_view" or "utf8_view" or "utf8view" or "stringview" => "StringViewType.Default",
                "binary_view" or "binaryview" => "BinaryViewType.Default",
                "guid" => "GuidType.Default",
                "bool8" or "arrow.bool8" => "Bool8Type.Default",
                _ => $"StringType.Default /* unrecognized override: {typeName} */",
            };
        }

        private static (string Unit, string Timezone) ParseTimestampParams(string input, string prefix)
        {
            var unit = "Microsecond";
            var tz = "UTC";
            var inner = ExtractBracketParams(input, prefix);
            if (inner != null)
            {
                var parts = inner.Split(',');
                if (parts.Length >= 1) unit = NormalizeTimeUnit(parts[0].Trim());
                if (parts.Length >= 2) tz = parts[1].Trim().Trim('"', '\'');
            }
            return (unit, tz);
        }

        private static (int Precision, int Scale) ParseDecimalParams(string input)
        {
            var precision = 38;
            var scale = 18;
            // Match decimal128(p, s) or decimal(p, s)
            var start = input.IndexOf('(');
            var end = input.IndexOf(')');
            if (start >= 0 && end > start)
            {
                var parts = input.Substring(start + 1, end - start - 1).Split(',');
                if (parts.Length >= 1 && int.TryParse(parts[0].Trim(), out var p)) precision = p;
                if (parts.Length >= 2 && int.TryParse(parts[1].Trim(), out var s)) scale = s;
            }
            return (precision, scale);
        }

        private static string ParseUnitParam(string input, string prefix, string defaultUnit)
        {
            var inner = ExtractBracketParams(input, prefix);
            return inner != null ? NormalizeTimeUnit(inner.Trim()) : defaultUnit;
        }

        private static string ExtractBracketParams(string input, string prefix)
        {
            // Supports both [] and () delimiters: timestamp[us, UTC] or timestamp(us, UTC)
            var rest = input.Substring(prefix.Length).Trim();
            if (rest.Length >= 2)
            {
                if ((rest[0] == '[' && rest[rest.Length - 1] == ']') ||
                    (rest[0] == '(' && rest[rest.Length - 1] == ')'))
                    return rest.Substring(1, rest.Length - 2);
            }
            return null!;
        }

        private static string NormalizeTimeUnit(string unit)
        {
            switch (unit.ToLowerInvariant())
            {
                case "s": case "second": case "seconds": return "Second";
                case "ms": case "millisecond": case "milliseconds": return "Millisecond";
                case "us": case "microsecond": case "microseconds": case "μs": return "Microsecond";
                case "ns": case "nanosecond": case "nanoseconds": return "Nanosecond";
                default: return "Microsecond";
            }
        }

        private static string GetArrayBuilderType(TypeInfo type)
        {
            // Check for view type overrides
            if (type.ArrowTypeOverride != null)
            {
                var ov = type.ArrowTypeOverride.ToLowerInvariant().Replace("_", "");
                if (ov is "stringview" or "utf8view") return "StringViewArray.Builder";
                if (ov is "binaryview") return "BinaryViewArray.Builder";
            }
            return type.Kind switch
            {
                ArrowTypeKind.String => "StringArray.Builder",
                ArrowTypeKind.Bool => "BooleanArray.Builder",
                ArrowTypeKind.Byte => "UInt8Array.Builder",
                ArrowTypeKind.SByte => "Int8Array.Builder",
                ArrowTypeKind.Int16 => "Int16Array.Builder",
                ArrowTypeKind.UInt16 => "UInt16Array.Builder",
                ArrowTypeKind.Int32 => "Int32Array.Builder",
                ArrowTypeKind.UInt32 => "UInt32Array.Builder",
                ArrowTypeKind.Int64 => "Int64Array.Builder",
                ArrowTypeKind.UInt64 => "UInt64Array.Builder",
                ArrowTypeKind.Float => "FloatArray.Builder",
                ArrowTypeKind.Double => "DoubleArray.Builder",
                ArrowTypeKind.Half => "HalfFloatArray.Builder",
                ArrowTypeKind.Decimal => "Decimal128Array.Builder",
                ArrowTypeKind.DateTime or ArrowTypeKind.DateTimeOffset => "TimestampArray.Builder",
                ArrowTypeKind.DateOnly => GetDateArrayBuilderType(type),
                ArrowTypeKind.TimeOnly => GetTimeArrayBuilderType(type),
                ArrowTypeKind.TimeSpan => "DurationArray.Builder",
                ArrowTypeKind.Guid => "GuidArray.Builder",
                ArrowTypeKind.Binary => "BinaryArray.Builder",
                ArrowTypeKind.Enum => "StringArray.Builder",
                _ => $"StringArray.Builder /* fallback for {type.Kind} */",
            };
        }

        /// <summary>
        /// Returns the Arrow array type name for reading, respecting view type overrides.
        /// </summary>
        private static string GetArrayReadType(TypeInfo type)
        {
            if (type.ArrowTypeOverride != null)
            {
                var ov = type.ArrowTypeOverride.ToLowerInvariant().Replace("_", "");
                if (ov is "stringview" or "utf8view") return "StringViewArray";
                if (ov is "binaryview") return "BinaryViewArray";
            }
            return type.Kind switch
            {
                ArrowTypeKind.String => "StringArray",
                _ => null!,  // Not used for non-string types
            };
        }

        // --- Output helpers ---

        private void Line(string text = "")
        {
            if (string.IsNullOrEmpty(text))
            {
                _sb.AppendLine();
                return;
            }
            _sb.Append(' ', _indent * 4);
            _sb.AppendLine(text);
        }
