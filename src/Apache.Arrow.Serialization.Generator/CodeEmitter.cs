        private string GetArrowTypeExpression(TypeInfo type)
        {
            if (type.ArrowTypeOverride != null)
                return ParseArrowTypeOverride(type.ArrowTypeOverride);

            return type.Kind switch
            {
                ArrowTypeKind.String => "StringType.Default",
                ArrowTypeKind.Bool => "BooleanType.Default",
                ArrowTypeKind.Byte => "UInt8Type.Default",
                ArrowTypeKind.SByte => "Int8Type.Default",
