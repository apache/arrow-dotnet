                break;
            }
            case TypeKind2.Bool:
            {
                var bl = GetBoolArrayInfo(prop);
                Line($"var bld_{index} = new {bl.BuilderType}();");
                break;
            }
            case TypeKind2.Byte:
                Line($"var bld_{index} = new UInt8Array.Builder();");
                break;
            case TypeKind2.SByte:
                Line($"var bld_{index} = new Int8Array.Builder();");
                break;
            case TypeKind2.Int16:
                Line($"var bld_{index} = new Int16Array.Builder();");
                break;
            case TypeKind2.UInt16:
                Line($"var bld_{index} = new UInt16Array.Builder();");
                break;
            case TypeKind2.Int32:
                Line($"var bld_{index} = new Int32Array.Builder();");
                break;
            case TypeKind2.UInt32:
                Line($"var bld_{index} = new UInt32Array.Builder();");
                break;
            case TypeKind2.Int64:
                Line($"var bld_{index} = new Int64Array.Builder();");
                break;
            case TypeKind2.UInt64:
                Line($"var bld_{index} = new UInt64Array.Builder();");
                break;
            case TypeKind2.Float:
                Line($"var bld_{index} = new FloatArray.Builder();");
                break;
            case TypeKind2.Double:
                Line($"var bld_{index} = new DoubleArray.Builder();");
