                    if (prop.IsNullable)
                        Line($"var prop_{index} = col_{index}.IsNull(row) ? (System.DateOnly?)null : col_{index}.GetDateOnly(row);");
                    else
                        Line($"var prop_{index} = col_{index}.GetDateOnly(row)!.Value;");
                    break;
